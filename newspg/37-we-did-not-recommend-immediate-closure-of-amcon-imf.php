<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The Senior Resident Representative in Nigeria for the International Monetary Fund (IMF), Mr. Scott Roggers has said that the board of the multilateral institution was pleased with the success so far recorded by the Assets Management Corporation of Nigeria (AMCON) and has never recommended &lsquo;immediate closure&rsquo; of the corporation to the Federal Government of Nigeria.</p>
<p>
	Rather, after its Executive Board concluded the 2012 Article 1V consultation, the board acknowledged the fact that the Nigerian government has done a good job in setting up AMCON which soaked up toxic assets from the balance sheets of banks and restored stability to the banking system.</p>
<p>
	To this end, he said, the IMF feels that since major work of stabilizing the industry has been achieved and in line with the initial Act setting up AMCON, the corporation may soon begin the process of winding down between 2013 to 2017 and &ldquo;there is no controversy whatsoever between IMF and AMCON as insinuated in the report published on Monday.&rdquo;</p>
<p>
	Meanwhile, AMCON said yesterday it has stopped buying bad loans. &ldquo;We are not buying any more non-performing loans,&rdquo; its Managing Director, Mustafa Chike-Obi said, adding that it had not done so for six months. &ldquo;We have cleaned up the banking system as bad loans are under 5 per cent and we want to make sure that everybody adheres to the prudential guidelines,&rdquo; he added.</p>
<p>
	There was also a report that about N1.7 trillion investments could be threatened if the government accedes to IMF advice for the immediate or sudden closure of AMCON considering that it was yet to substantialy recover bad loans it purchased from banks. The amount was the discounted value used by AMCON to acquire liabilities of rescued and other financial institutions in the country estimated at over N4.3 trillion.</p>
<p>
	The IMF Resident Representative said though AMCON is still in the process of recovering bad debts it acquired, it is unlikely that all the debts would be recovered, &ldquo;and nobody does that. &ldquo;We think that given the volume of the bad debts which are far below the actual amount declared by the banks as revealed by AMCON, it may take AMCON close to four years from now to recover a substantial part of the debts depending on the approach. &ldquo;We have never raised any concern about N1.7 trillion investment.</p>
<p>
	The said statement was a part of the Public Information Notices (PINs) after the executive board discussions of general policy matters, which were not officially forwarded to the Nigerian government. We have been working with AMCON from the beginning and we are pleased with the success it has achieved so far,&rdquo;</p>
<p>
	Roggers said some days back On February 6, 2013, the Executive Board of the IMF concluded its 2012 Article 1V consultation with Nigeria and the outcome of the discussion was up loaded in the agency&rsquo;s website on March 28 2013. The board assessed overall macroeconomic performance in Nigeria and concluded that it had been broadly positive over the past year and suggested gradual winding down of the operations of AMCON to avoid &ldquo;moral hazards and financial problem.&rdquo;</p>
<p>
	The Assets Management Corporation of Nigeria (AMCON) Bill was passed in June 2010, following its harmonization by the National Assembly and its adoption by the senate. The Bill, which originally stipulated a 10-year lifespan was signed into law by President Goodluck Jonathan on July 19, 2010. Information obtained from AMCON&rsquo;s website yesterday indicated that the corporation set up by the CBN and the Ministry of Finance for cleaning bank&rsquo;s Non-Performing Loans (NPLs) will not have its existence terminated by 2020 as the Act establishing it initially envisaged.</p>
<p>
	The information which was attributed to the Director of Risk Management CBN, Kemi Fatogbe, maintained that AMCON&rsquo;s activities has reduced the ratio of NPLs from 30 per cent in 2009 to 5 per cent as of end 2012, adding that &ldquo;the 10-year lifespan for AMCON is out. We are still working on an operating assumption for AMCON of between 10-15 years but the corporation will not cease operations after that; it will remain to help ensure the financial system stability,&rdquo; Fatogbe was quoted as saying.</p>
<p>
	AMCON&rsquo;s chief, Chike-Obi, had in August last year anticipated that in 8 to 13 years, the crisis in the banking industry would be over. &ldquo;Everybody will be fine, the CBN will be paid back, and the government will be paid back,&rdquo; the AMCON boss had said. Under Article 1V of IMF&rsquo;s Agreement, the IMF holds bilateral discussions with members, usually every year. A staff team visits the country collects economic and financial information and discusses with officials the country&rsquo;s economic developments and policies. On return to headquarters, the staff prepares a report which forms the basis for discussion by the executive board.</p>
