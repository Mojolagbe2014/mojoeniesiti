<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	Members of the Institute of Chartered Accountants of Nigeria (ICAN) have been enjoined to continue to practice as tax consultants and administrators thereby compounding the controversy of taxation practice jurisdictional dispute between the Chartered Institute of Taxation of Nigeria (CITN) and the accounting body.</p>
<p>
	At the induction ceremony of 797 Fellows in Lagos over the weekend, the 48th president of the institute, Adedoyin Idowu Owolabi, emphasized that &ldquo;as chartered accountants, members of the institute can practice as tax administrators and consultants without being members of the CITN,&rdquo; insisting that the court judgment empowering chartered accountants to do so has been uploaded in the institute&rsquo;s website.</p>
<p>
	He said the Federal Inland Revenue Service Act specifically lists professional bodies that can practice taxation in Nigeria, and chartered accountants are one of them. Both ICAN and CITN have been celebrating the recent ruling by the Appeal Court sitting in Lagos, claiming the same ruling was in their favour and as such, they are laying respective claims to be the umbrella organisation regulating taxation practice in the country.</p>
<p>
	CITN opposed ICAN&rsquo;s continued regulation of taxation practice in 2004 and approached the Lagos High Court the following year to make a declaration that it is only CITN that can regulate taxation profession in the country by virtue of its enabling Act.</p>
<p>
	In 2007, Justice Lateefat Okunnu ruled that taxation is recognised as a legal profession separate from accounting and as such the power to regulate taxation practice and profession is vested in only CITN and not jointly with ICAN. She also ruled that it is illegal for any member of ICAN who is not also a member of CITN to practice taxation in return for a fee in the country.</p>
