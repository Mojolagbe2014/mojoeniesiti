<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	Technical, vocational and entrepreneurial education is key to Nigeria&rsquo;s bid to grow her economy and become one of the best top 20 economies in the world in 2020. The Director-General, National&nbsp; Office&nbsp; for Technology Acquisition&nbsp; and Promotion&nbsp; NOTAP, Federal Ministry of Sciences and Technology, Abuja,&nbsp; Dr. Umar&nbsp;&nbsp;&nbsp; Bindir stated this&nbsp; while delivering&nbsp; the Yaba College of Technology&rsquo;s&nbsp; 28th convocation lecture, titled&nbsp; &quot;Synergy in Technical , Vocational&nbsp; and Entrepreneurial&nbsp; Education&nbsp; for Industrial&nbsp; Growth -&nbsp; Panacea to Survival of Small&nbsp; and Medium&nbsp; Scale Business.&nbsp;</p>
<p style="text-align: justify;">
	Dr Bindir lamented that Nigeria has not been able to take any of her local or international development policy to greater height because of the weak educational institutions, especially in technical and vocation areas. He noted that the weakness of the nation&rsquo;s educational institution was a clear threat to the achievement of the dream of ranking among best top 20 economies in the world in 2020.</p>
<p style="text-align: justify;">
	According to him,&nbsp; the attainment of sustainable economic growth is dependent on the vibrancy of the nation&rsquo;s technical, vocational and entrepreneurial capacities as t is the master key for any meaningful&nbsp; development. A viable&nbsp;&nbsp; strategy to create efficient and effective synergy among technical vocational and entrepreneurial education is&nbsp;&nbsp; necessary for industrial growth.</p>
<p style="text-align: justify;">
	in his view, to ensure that we build the required critical mass of highly skilled manpower, we must be very innovative, and to be innovative, we must strengthen our education system. We must mount deliberate programmes&nbsp; to intensify and proudly&nbsp; promote technical ,vocational&nbsp; and entrepreneurial&nbsp; skills emerging&nbsp; from&nbsp; national innovation&nbsp; system, especially&nbsp;&nbsp; through&nbsp; viable&nbsp; partnerships, synergies&nbsp; and networking of related&nbsp; institutions&nbsp; on both&nbsp; supply&nbsp; and demand.</p>
<p style="text-align: justify;">
	He said for the country to develop, acquire and transfer technology, it must ensure a clear linkage between academia and industry by moving the academia to certain research centres. According to him, there is need to establish functional technical colleges, effective vocational centres and entrepreneurial courses in all higher institutions of learning in the country to build capacities.</p>
<p style="text-align: justify;">
	The Director-General&nbsp; also said that for Nigeria to achieve a sustained&nbsp;&nbsp; economic development, it must revisit her educational policy to incorporate an effective platform for technical, vocational and entrepreneurial studies. He said investment in technical, vocational and entrepreneurial education must be sustained as a priority to facilities the creation of critical mass manpower required in the industrial sector.</p>
<p style="text-align: justify;">
	According to him, Nigeria needs to establish national technical education council at the federal level to be headed by the president, with similar structure at the states to be chaired by the governors. He stated further that with the establishment of such bodies, the nation will receive a boost and the industrial sector will get better.</p>
<p style="text-align: justify;">
	The DG also called on government at level to subsidise the vocational and technical education programme in the country to encourage qualified people to take up to the trades. For students who show promises in technical and vocational education, he urged government to give them&nbsp;some financial incentives to boost their interest.</p>
<p style="text-align: justify;">
	He opined that if the country has a strong&nbsp;work force&nbsp;knowledgeable in technical and vocational&nbsp;areas, they would immensely contribute to growth of business and industry in many ways. He stated that for the country&rsquo;s economy to grow Nigeria must understand the need to invest heavily in the education sector for acquisition of&nbsp;&nbsp; technical manpower, vocational skills and entrepreneurial to man the industrial outfits of the nation. According to him, it is only when the country evolves a strong technological manpower and skills that it can have virile and strong industrial sector.</p>
<p style="text-align: justify;">
	Dr Bindir said further&nbsp; stated that&nbsp; only sustained&nbsp; investment in technical education would&nbsp; propel technical and vocational&nbsp; skills to drive&nbsp; industrial production, generate jobs&nbsp; and create wealth for the nation&rsquo;s&nbsp; economic&nbsp; development. He added that sound vocational and technical education is the master key and the brain behind modernising and developing&nbsp;viable&nbsp;economies. For countries&nbsp;where these are well understood and&nbsp;developed, the technical, vocational&nbsp;and entrepreneurial&nbsp;education is well connected and networked and these in turn&nbsp;propel innovation and prosperity.</p>
<p style="text-align: justify;">
	In conclusion, the DG asserted that knowledge institution such as universities must transform&nbsp; their operations&nbsp; to emerge&nbsp; not just&nbsp; as facilities&nbsp;&nbsp; for students to come in and graduate with certificates, but as a training centres for&nbsp; the&nbsp; building of&nbsp; individual capacities to&nbsp; become self-reliant.</p>
