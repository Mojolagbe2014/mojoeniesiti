<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	A Federal High Court sitting in Abuja has awarded more than N200 million cost against NICON Insurance and National Insurance Commission (NAICOM) in favour of the Federal Inland Revenue Service (FIRS).</p>
<p>
	The presiding judge, Justice Adeniyi Ademola, also granted the claims of FIRS of 10 per cent interest from February 2008 to date and 10 per cent post-judgment sum. A cost of N50,000 each was awarded against NICON Insurance and NAICOM.</p>
<p>
	In a statement, Director, Communications and Liaison Department, Mr Emmanuel Obeta, said that FIRS had through a writ of summons, signed by Idrissa Kogo of the Legal and Prosecution Department supported by 33-paragraph affidavit deposed to by Omofoyewa Taofeek Laide, claimed the sum of N99, 917,061.80 against the defendants as a balance of its claims under the Staff Welfare Insurance Scheme with the NICON Insurance.</p>
<p>
	The service sought a 10 per cent interest per annum from February 2008 till judgment is entered and 60 per cent interest on the judgment sum until it is liquidated.</p>
<p>
	Also, FIRS sought for N100 million as general damages against the defendants for wrongfully holding on to the Service&rsquo;s N99.9 million despite repeated demands for the said amount.</p>
<p>
	The dispute between FIRS and NICON Insurance arose when FIRS noted that NICON was unable to meet its financial commitments to FIRS beginning between 2007 and 2008. FIRS told the court it contributed a total of N220 million to the scheme-from staff entitlements and benefits, designed to mortgage the funds of the scheme for the benefit of FIRS contributing workforce. The scheme kicked off in 1997, when FIRS paid the sum of N10 million to NICON.</p>
<p>
	At a reconciliation meeting between FIRS and NICON on February 27, the sum of N155, 417,061.80 million was established as NICON&rsquo;s indebtedness to FIRS. NCON paid an installment of N23 million in March 2008 and another N15 million in May 2008.</p>
<p>
	Between 2008 and December 2010, a total of N17. 5 million was paid to FIRS, leaving a balance of N99, 917,061.80, which led to FIRS&rsquo; suit. At the hearing of the case, FIRS counsel, and an Assistant Director, Legal and Prosecution Department, Mr. James Binang, in his reply argued that &ldquo;by a group policy No 2003038, the plaintiff&rsquo;s staff entrusted the fruits of their labour of yester-years into the statutory hands of the defendants who denied them the benefits of their savings during the rainy days.</p>
<p>
	&ldquo;The plaintiff thereafter tried to enforce its rights under the said Group Welfare Insurance Policy No 2003038 with the defendant who now denied that it ever knew the plaintiff&rsquo;s claim.</p>
<p>
	&ldquo;This Honorable Court remains the last bastion of the hope for the plaintiff in its effort to ventilate its right under the aforesaid Group Welfare Insurance Scheme now poorly managed by the NICON Insurance&rdquo; he said.</p>
<p>
	Citing Yabola Ltd Vs Trade Bank Plc (1998) 6 NWLR (PT 555) @ 670 and Bature V Savannah Bank of Nigeria Ltd (1998) 4 NWLR (PT 546, FIRS contended that &ldquo;where there is an admission of claim under the undefended list, the court is duty bound to enter judgment.&rdquo;</p>
<p>
	However, the defendants in their objections challenged the jurisdiction of the court to entertain and determine the suit. Specifically, the second defendant (NAICOM) urged the court to dismiss the suit on grounds that there was no cause of action and that it was wrongly joined in the matter.</p>
<p>
	In an affidavit to defend filed by Ijeoma Nwankwo, the first defendant (NICON) claimed that FIRS suit was time-barred as the cause of action arose on May 19, 2010, while Section 26 (1) of the NICON Act Cap N54 Laws of the Federation of Nigeria, 2004 stipulates that a suit must be instituted within 12 months.</p>
<p>
	Ijeoma maintained too that NICON usually receives premium of insurance cover before insurance policy is issued and that &ldquo;the plaintiff never paid any premium towards any type of insurance cover including any staff insurance welfare scheme or for the insurance of any policy.</p>
<p>
	It claimed FIRS never paid its premium as and when due. At the close of hearing, Justice Ademola Adeniyi assented to FIRS prayers and entered judgment in favour of FIRS.</p>
