<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The Nigerian Association of Chambers of Commerce, Industry, Mines and Agriculture (NACCIMA), in partnership with the Export-Import Bank (NEXIM) has confirmed plans to go to the Stock Market to source funds for its planned transnational shipping company.</p>
<p>
	&nbsp;</p>
<p>
	The new shipping company, which has been estimated to cost $60 million, is expected to ease business transactions within the west and central African sub-regions. The project, according to the NACCIMA Director General, Dr.John Isemede, would be with cooperation from the West and Central African Organised Private Sector (OPS), which include countries like Zaire, Congo DRC, Niger, Togo, Ghana and others within the regions.</p>
<p>
	&nbsp;</p>
<p>
	The NACCIMA boss noted that the shareholders would be the rightful owners of the transnational shipping line project to be named &lsquo;Sealink&rsquo;. Isemede said &ldquo;We want as many people to be involved in the project, that is why we are going to the Stock Exchange to throw the ownership open and encourage Nigerians to share in the dream,&rdquo;.</p>
<p>
	&nbsp;</p>
<p>
	He said further that said the idea was also to discourage monopoly, stating that some investors with huge capital have already started showing interests, but NACCIMA would still prefer to have as many owners of businesses in Nigeria buying into the project, rather than being controlled by minority. He expressed the belief that the Sealink Shipping project would be a success story just like the ECOBANK Transnational which was conceived by NACCIMA some years ago, but now a household name in the region.</p>
<p>
	&nbsp;</p>
<p>
	&ldquo;We believe that it will be an instant success because the West African market is huge and had not been fully tapped as a result of challenges being faced when goods and persons are being transported due to time of freight and the attendant high cost due to the absence of direct shipping line plying the West and Central African corridor,&rdquo; he stated. He added that the shareholders would not be limited to Nigeria as the Sensitization / Pre Investors&rsquo; forum held in Lagos last week has been on-going in other countries of the participating regions.</p>
<p>
	&nbsp;</p>
<p>
	Though he said he could not confirm when the company would go into the market, the NACCIMA Director General said First Registrar is expected to be the Issuing House for the Sealink proposed shares. The project which equally aims at facilitating and enhancing trade within the Economic Community of West African State (ECOWAS) and Central Africa States, is expected to be ready by the fourth quarter and will be funded through $36 million equity and $24 million through borrowing.</p>
<p>
	&nbsp;</p>
<p>
	The Managing Director, NEXIM Bank, Mr Roberts Orya said NEXIM&rsquo;s involvement was to encourage and facilitate trade within the ECOWAS Sub-region, adding that this will also encourage exportation of local goods from Nigeria. According to him, the project would break trade barriers such as delay in clearing of goods, high transportation costs, high logistics costs, excessive tax, high cost of freight, among others. Orya noted that $36 million will be used to procure vessels, equipment, office space and other infrastructure, while $24 million will be used as working capital to cover general and administrative expenses .</p>
<p>
	&nbsp;</p>
<p>
	&ldquo;The African Development Bank (ADB) is concluding arrangements toward a technical assistance to the project, while the World Bank has also requested for an update on the study with a view to providing technical assistance to the realization of the project. &ldquo;Institutional funding supports are also being pursued with multilateral financial institutions like the ECOWAS Bank for Investment and Development (EBID), African Export-Import Bank and West African Development Bank (BOAD),&rdquo; he added.</p>
