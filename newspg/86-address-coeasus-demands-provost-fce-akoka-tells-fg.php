<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;">Provost, Federal College of Education (FCE) Technical, Akoka, Dr. Sijibomi &nbsp;Olusanya has appealed to Federal Government to quickly dialogue with the Colleges of Education Academic Staff Union (COEASU) to end the ongoing strike action before things gets worse.<br />
<br />
Dr. Olusanya&nbsp;said government should expediently discuss with the union to end the strike embarked by the union so that what happened in the public universities do not repeat itself in the Colleges of Education.<br />
<br />
Barley two weeks after the Academic Staff Union of Universities (ASUU) suspended its 159 days strike; lecturers in the colleges of education commenced an indefinite strike to compel Federal Government to address decay in the colleges of education.<br />
<br />
The Provost warned Federal Government to be very careful not to allow the strike to continue for a long time as it happened in the public universities because students cannot be kept at home again.<br />
<br />
According to him, Colleges of Education system is very critical to nation&rsquo;s education development, because they &nbsp;&nbsp;produce those who teach the nation and without college of education the country cannot function effectively.<br />
<br />
According to him, government should not allow this strike to linger for so long, because&nbsp;&nbsp; we have just finished with ASUU&rsquo;s strike and having another strike in the nation&rsquo;s tertiary institutions is not good for our educational development.<br />
<br />
The Provost stated that college of education are very critical to the national development and federal government should endeavour to give attention to the issues raised by union to expediently end strike before it gets out of hands.<br />
<br />
The union had in a letter to federal government three weeks ago&nbsp;lamented that the Federal Government had failed to address issues it&rsquo;s raised on how to revitalise college of education in the country.<br />
<br />
The union&rsquo;s complaints bordered on infrastructural decay, poor funding, non-implementation of the 2010 FG-COEASU agreement, poor conditions of service, brain drain and illegal imposition of the Integrated Personnel and Payroll Information System. The union stated that the strike is to salvage the institutions from total collapse.<br />
<br />
The union&rsquo;s &nbsp;demands &nbsp;among others includes, the suspicious delay in the release of the 2012 visitation white paper; the refusal of colleges managements to recruit commensurate academic manpower to cater for the high dearth of academic staff to meet the contemporary challenges engendered by the rising population of students; the non-harmonization of conditions of service for academic staff of all tertiary institutions to stem brain drain from the colleges of education to the universities; the proposed selective imposition of IPPIS in the Colleges of Education &nbsp;(CoE )and the polytechnics; the truncation of a hitherto functional and innovative Tertiary Education Trust Fund leading to obvious regrettable gaps that compromises the critical need for the consolidation of the gains recorded during the past five years; non implementation of CONPCASS, 65years retirement age and Migration to CONTISS 15 in some state CoE; the non-conduct of Needs Assessment for CoE to address students and staff needs for the enhancement of quality teaching and learning.<br />
<br />
Meanwhile, the Federal Government in a statement last Wednesday reinstated its commitment to the resolution of the strike embarked upon by Colleges of Education Academic Staff Union. According to the statement, Federal Government through Federal Ministry of Education and Federal Ministry of Labour is in continuous engagement with officials of the union to end the strike action.<br />
<br />
The statement further stated that Federal Government has resolved most of the key issues that led to the strike action and would in due course resolve the outstanding issue. According to the statement by the Federal Government, Nigerians are aware that the Federal Government has already set up the Needs Assessment Committees for Federal Polytechnics and Federal Colleges of Education. Both committees have been inaugurated and are working to deliver on their mandate.</div>
