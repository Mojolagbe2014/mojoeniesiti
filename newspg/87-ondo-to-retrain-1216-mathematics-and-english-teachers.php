<br />
Selecting two mathematics and two English teachers from each of the schools, the state expects to fully prepare a total of 1,216 teachers to lift the performance of students in the state in the subjects before the end of June 2014.<br />
<br />
Commissioner for Education, Mr. Jide Adejuyigbe, during the opening of the training on Tuesday in Akure, said the state government had decided to pay the fees for candidates registering for the 2014 May/June WASSCE from the 304 public secondary schools in the state.<br />
<br />
The commissioner said all these interventions point to one incontrovertible fact - The need to attain excellence in our education service delivery by way of measurable improvements in the performance of our public school products in public examinations and academic and allied competitions.<br />
<br />
According to him, It is on this note that this workshop has been packaged to assist subject teachers in repositioning the teaching of Mathematics and English Language in our secondary schools. The training is expected to be held in the state&rsquo;s three senatorial districts between January 7 and 9, 2014.</div>
