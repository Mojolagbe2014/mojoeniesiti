	The National Insurance Commission (NAICOM) has concluded plans to review the Market Development and Restructuring Initiative (MDRI) which was designed to enhance the industry&#39;s growth. This was disclosed by the Commissioner for Insurance Mr Fola Daniel. Mr Daniel disclosed that NAICOM will next year review the operational guidelines of the initiative to align it for better performance.</p>
<p style="margin: 1em 0px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif; line-height: 18px; text-align: justify;">
	The NAICOM boss who also said that the review is one of the commission&#39;s programme for the new year however declined to give the time table for the review and when the expected guidelines would be released. The review, he said, has become necessary due to the failure of the industry to achieve some of its projections&nbsp; in the first phase where a target of N1 trillion premium income could not be achieved.<br />
	<br />
	In a statement, Managing Director, Riskguard-Africa Nigeria Limited, Chief Yemi Soladoye said that the delay in the implementation of the initiative has affected the projections set to be achieved, adding that the programme was meant to commence in 2009, but never took-off until 2011. He noted further that to recover the lost period, there should be a shift in the deliverables to make-up for the difference between the time of the strategy crafting and implementation.<br />
	<br />
	Chief Yemi Soladoye also said if that the N1 trillion premium income projection for the period was to be achieved with a four-year strategic plan, there is no way the target will be achieved with the commencement of implementation of the initiative a year to the set deadline. He said &quot;Most people are reading the strategy document and not relating it to when implementation took off. If there is a projection that in the next four years, we will get N1 trillion and as we could see from the paper, we were to start in 2009; we had what we are to achieve in 2009, 2010, 2011 and 2012. So, N1 trillion is in year four which is 2012.</p>
<p style="margin: 1em 0px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; color: rgb(0, 0, 0); font-family: Arial, Helvetica, sans-serif; line-height: 18px; text-align: justify;">
	According to Chief Yemi Soladoye, If implementation started in 2011, it will be a case of shifting the deliverables forward base on the difference on the ground between the strategy crafting and the implementation. The initiative is remarkable in the history of the industry and it is also an evergreen thing.&quot; He however noted that the initiative cannot be wished away, as it has brought about many developments, adding that efforts by NAICOM to reposition the industry through micro-insurance, Takaful and more are strategic plans stated in the MDRI document.</p>
