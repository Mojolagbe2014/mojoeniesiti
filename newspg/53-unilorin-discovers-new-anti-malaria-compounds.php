<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The University of Ilorin (Unilorin) has discovered new anti-malaria compounds. Through the World Bank-assisted Science and Technology Education Post-Basic Project (Step-B), University of Ilorin may soon introduce new anti-malaria derivatives into the country&rsquo;s pharmaceutical industry. This is being made possible by the 300 new compounds discovered at the laboratory of Prof. Joshua Ayoola Obaleye, one of the Step-B funded researchers at the institution. &nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="text-align: justify;">
	Malaria is an infectious disease characterised by cycles of chills, fever, and sweating, caused by a protozoan of the genus Plasmodium in red blood cells, which is transmitted to humans by the bite of an infected female anopheles mosquito, and it is a scourge in tropical Africa. The disease is particularly dangerous when children and pregnant women are attacked.&nbsp; &nbsp;</p>
<p style="text-align: justify;">
	Prof. Obaleye disclosed recently that the new compounds have been tested on animals and microbes and found to be very successful, stressing that the next stage is to try it on humans. He added that his new discoveries are of higher value than existing derivatives, stating that after patenting, the available compounds can be mass produced for the pharmaceutical industries for further processing into anti- malaria drugs.</p>
<p style="text-align: justify;">
	The don called on Nigerian philanthropists to come to the aid of researchers by funding the research more as it is being done in&nbsp;developed economies. Eulogizing the wisdom behind the Step-B project, the professor of Inorganic Chemistry, who is a visiting Professor to many Universities in India, Zimbabwe, Italy and Israel stated that before Step-B, things were very difficult to achieve.</p>
<p style="text-align: justify;">
	According to him, though they travel outside the country for one award or the other, they still need to beg for global attention and recognition. &nbsp;&ldquo;In the past, I used to write and write for my articles to be published in journals. Now, I publish in journals very easily. Colleagues in foreign countries can follow activities of my laboratory from these journals. They get the same result and thus appreciate our efforts. &ldquo;Step-B is an organisation that has helped us move forward in several areas of research.</p>
<p style="text-align: justify;">
	Many problems afflicting researchers in this country were resolved under Step-B. &ldquo;From Step-B, he stated further, I now have several research collaborators from outside the country. These people help to supervise postgraduate students who are working in this laboratory. One of them who incidentally is on ground is Prof. Avinash Kumbhar of the Department of Chemistry, University of Pune, India&rdquo;.</p>
<p style="text-align: justify;">
	Prof. Obaleye also disclosed that many researchers in Nigerian universities like the University of Ibadan, University of Jos, and the Delta State University come freely to use facilities in his Step-B equipped laboratory, stating that they are not collecting a penny from anyone as&nbsp;Step-B gives&nbsp;them freely and they have to give it out freely. According to him, inside the well-stocked laboratory located at the Chemistry Department, there are facilities comparable to many others worldwide such as Spectrophotometer, Infrared Spectrophotometer, Gas Chromatograph and atomic absorption Spectrophotometer.</p>
<p style="text-align: justify;">
	One of Prof. Obaleye&#39;s collaborators, Prof. Kumbhar, who was busy in the laboratory disclosed plans to elevate the collaboration between him and the Unilorin researcher to institutional level whereby the University of Pune, India and the University of Ilorin would, through a Memorandum of Understanding (MOU), work out staff and students development exchange programmes.</p>
<p style="text-align: justify;">
	Responding to questions on scholarships, fellowships and grant opportunities to Nigerian students from Indian government, the visiting Professor, who is also the Coordinator of National Centre for Free Radical Research (NCFRR), India, discussed the opportunities provided by the BST-TWAS funding partnership. BST-TWAS, according to him, is an Indian government partnership with an agency of the United Nations Educational Scientific and Cultural Organization (UNESCO).</p>
