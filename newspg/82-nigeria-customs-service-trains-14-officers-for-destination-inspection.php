<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The Comptroller-General of the Nigeria Customs Service (NCS), Alhaji Abdullahi Dikko, has revealed that 14,000 officers of the service had been trained to take over Destination Inspection at the ports.</p>
<p style="text-align: justify;">
	Dikko disclosed this at a news conference in Lagos, after a three-day campaign on the Pre-Arrival Assessment Reports (PAAR) scheme put in place by the service. He said NCS was ready to take over destination inspection as it had for example trained more than 300 officers to scan and 350 others to process PAAR.</p>
<p style="text-align: justify;">
	The comptroller-general said also that some other officers had been redeployed to different departments. According to him the responsibility of service providers before now is about assessment, classification, goods of origin, risk management and all aspects of the statutory responsibilities of the Nigeria Customs Service. We were left with only examination. By implication today, the Federal Government has returned all these statutory functions to the Nigeria Customs Service.</p>
<p style="text-align: justify;">
	The comptroller-general believes the government measures the services&#39; preparedness in taking over while they had the visiting World Bank officials almost four times during the period of taking over. The world bank team certified that the Nigeria Customs Service is fully ready to take over.</p>
<p style="text-align: justify;">
	The comptroller-general therefore called on Nigerians to show understanding, saying, the beginning is always difficult and we also call on members of the press to help us inform the Nigerian public about where we were before and where we are going.</p>
<p style="text-align: justify;">
	According to Dikko, for one thing, it is an advantage that the Nigeria Customs Service taking over. You are very much aware that the Nigerian government is spending a huge amount of money in foreign currency to pay service providers before now. But today, this payment ceased to exist. So, by implication, Nigeria is going to get more revenue. The money that is being paid to these people will now go to the coffers of government.</p>
<p style="text-align: justify;">
	&nbsp;The comptroller-general, who said the service providers were operating with 21 scanners, added that the NCS was prepared to provide 50 scanners within the next five years. He said this would be a consequence of the take-over of destination inspection by the service.</p>
<p style="text-align: justify;">
	&nbsp;Dikko said the Customs had taken over the available scanners, saying that the service providers were left with maintenance of the scanners for six months before the full take-over. He said also that the NCS had prosecuted 15 persons suspected to have hacked into the service&rsquo;s trade hub. The Forensic Unit of the Customs has been directed to deal with the internet hackers,&rsquo;&rsquo; the comptroller-general said.</p>
<p style="text-align: justify;">
	Mr Bashar Yusuf, the NCS&rsquo;s Deputy Comptroller, ICT Operations, said the introduction of the Trade Hub by the Customs would ease cargo clearance and import processing. He said the NCS had provided two free internet access for the Balogun Business Association (BBA) at the Lagos Trade Fair Complex, for easy access to the Customs Trade Hub. Yusuf said the traders would have their cargoes released immediately from the port with the genuine documents processed through PAAR. He said cargoes would be cleared before arrival, once other government agencies operating at the port confirmed the documents through PAAR, An Assistant Comptroller-General in the service, Alhaji Abubakar Umar, said NCS had purchased many stand-by generators and also provided internet backup in case of power failure.</p>
<p style="text-align: justify;">
	Mr Adewale Adeniyi, the National Public Relations Officers of the NCS, said the service had gone through series of campaign programmes for traders in their local languages. He said it was the directive given to officers by the Comptroller-General of Customs that all traders should be addressed in local languages for effective understanding about PAAR.</p>
<p style="text-align: justify;">
	According to Adeniyi, the comptroller-general has promised to extend the campaign to the Eastern and Northern states of the country.</p>
