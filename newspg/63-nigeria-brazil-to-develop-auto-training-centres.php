<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	Nigeria is to collaborate with Brazil to develop training centres in spare parts production and auto maintenance, the Minister of Industry, Trade and Investment, Mr. Olusegun Aganga, has said.</p>
<p style="text-align: justify;">
	Brazil is the production home to a number of luxury buses, including the Marcopolo brand being used on long distance travels and intra-city mass transit in Nigeria.</p>
<p style="text-align: justify;">
	Aganga, who spoke recently in Calabar, Cross River State, at the Industrial Training Fund 19th Biennial Training Conference on Skills Acquisition and Job Creation, said following the discussion between the two countries, the Federal Government had finalised plans to create three new automobile clusters and training centres in the country.</p>
<p style="text-align: justify;">
	He said they would be sited in Lagos, Anambra and Kano states as part of the National Industrial Revolution Plan and Industrial Skills Development Programme.</p>
<p style="text-align: justify;">
	The minister reaffirmed the commitment of President Goodluck Jonathan&rsquo;s administration towards tackling unemployment through the implementation of an integrated, industrial skills and manpower development programme.</p>
<p style="text-align: justify;">
	He however said that the Federal Government had no plan to create a new agency for industrialisation as being speculated but was considering transforming an existing agency under the supervision of the ministry (with a related mandate) to drive the implementation of its industrialisation policies. This, he stressed, was in line with an agreement reached at a stakeholders&rsquo; meeting on the NIRP about two months ago.</p>
<p style="text-align: justify;">
	He said further that&nbsp; &ldquo;as part of our National Industrial Skills Development Programme and the Nigeria Industrial Revolution Plan, the Industrial Training Fund is collaborating with Brazil to develop three automobile training centres and clusters across the country that will meet international standards&quot;.</p>
<p style="text-align: justify;">
	The centres will be in Lagos (Ibadan Zone), Nnewi (Aba Zone) and Kano, Kaduna zone automobile clusters. The automobile training centres will train Nigerians in auto maintenance and spare parts production.</p>
<p style="text-align: justify;">
	The minister concluded by saying that &quot;for us to successfully diversify our economy, create jobs and generate wealth for our country, industrialisation remains our only hope for the future. In order to achieve this, we need to build strong institutions to drive our industrialisation process just as it was done in other industrialized countries&rdquo;.</p>
