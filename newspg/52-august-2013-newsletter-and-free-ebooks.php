<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	<span style="font-size: 12px;"><span style="color: rgb(0, 0, 205);">You can still achieve Your Goals for the Year!</span></span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">As the year draws to a close, anyone would be justifiably anxious, and indeed apprehensive, wondering if he/she would still be able to accomplish the goals earlier he/she had set for the year. Feeling a little desperate? No need. You have come this far on your first steam. Maybe it&rsquo;s time to take a breather and reconsider your strategies.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">If you&#39;re like most successful people, you started the year by creating a plan. You know that not having a plan is like being lost with no map or GPS; there&#39;s a slight possibility of getting to your destination but it&#39;s more difficult and less likely.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">Unfortunately, while many people have a plan, many have trouble following it. Like your New Year&#39;s resolutions, the plan was easy to follow in January, but each passing month brought with it new opportunities and new challenges. As a result, by this time of the year many people have shifted their focus from the well plotted course to the new realities of their lives/careers.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">For example, you might have planned to grow your career this year by attending a number of courses or getting additional qualifications, but you didn&#39;t expect so many things to derail your plan. Such as an increased barrage of emails, transfer to a new location, increased workload due to firing/resignation of a colleague, or a technical glitch with which you had to deal.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">Are you feeling off course? You are not alone in this. Most people have also gotten off track from the plans they laid out in January. What is required here is a little bit of course-correction.&nbsp; According to <a href="http://www.inc.com/marla-tabaka/ways-to-achieve-your-year-end-goals.html">Dave Lavinsky</a>, President and Founder of Growthink, most entrepreneurs have gotten off track from the plans they laid out in January. &nbsp; &quot;We see this a lot,&quot; says Lavinsky. &quot;If business owners (<em>or individuals</em>) don&#39;t course-correct now they will end the year no better off than they started it. Or at least not in the position they could have attained.&quot;</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">The good news is that one-third of the year is still left, which is plenty of time to accomplish some or most of the goals you previously set for 2013. Here are the simple steps that Lavinsky recommends to get you back on track.</span></p>
<p style="margin-left:.5in;">
	<span style="font-size: 12px;"><strong>1.</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Make a new list.&nbsp;</strong></span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">The first step in this course correction process is to review the big annual goals that you set at the beginning of the year. Now create a new list. What can you realistically achieve in the remainder of the year?</span></p>
<p style="margin-left:.5in;">
	<span style="font-size: 12px;"><strong>2.</strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Think about how and why your two lists differ. </strong></span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">Why are certain goals no longer attainable? Did new opportunities get in their way? And are these new opportunities working out? More likely than not, any new, unplanned opportunities you pursued so far this year were mere distractions from your plan. Think about what else has gotten in the way of your goals. Have you spent too much time answering phone calls or emails? Have you been stuck in too many meetings, or had to work tirelessly to satisfy the needs of certain customers? Figure out what has caused certain goals to fall out of reach, and most importantly, what you can do differently to achieve them (such as holding off on new, unplanned opportunities, or only checking email once per day).</span></p>
<p style="margin-left:.5in;">
	<span style="font-size: 12px;">3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Next, create a plan for achieving your top goals this year.</strong></span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">Start by identifying the one goal you absolutely must achieve by the end of the year. Write down the five things you need to do in the next thirty days to progress toward achieving that goal. Then, after thirty days have passed, confirm you have achieved those five things and create the list of five things you must accomplish during the next month. Doing this will get you in the habit of constantly setting and accomplishing short-term goals that result in the achievement of your bigger goals. It will also help you figure out how to stop the myriad of distractions that needlessly burden most people.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">Remember, that one-third of the year is still left. We&#39;ve all seen sports teams make amazing progress in the final period and outperform their competitors. You can too if you create and stick to the right game plan.</span></p>
<p style="text-align: justify;">
	<span style="font-size: 12px;">On this positive note, we invite you to sit back and enjoy our featured story and articles for the month.</span></p>
