<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	Transparency International and Socio-Economic Rights and Accountability Project have called on the government of President Goodluck Jonathan to account for the huge sums of money that has been invested in the education sector and be serious about teaching children the value of honesty. The organizations also warned that corruption in education threatens children&rsquo;s prospects in Africa and called on governments to tackle the problem without further delay.</p>
<p style="text-align: justify;">
	At the media launch of the report Global Corruption Report: Education on Thursday in Abuja, the two organizations said that corruption has a devastating impact on developing nations, particularly in Africa, hindering progress towards the Millennium Development Goals and jeopardizing social and economic development.</p>
<p style="text-align: justify;">
	SERAP executive director Adetokunbo Mumuni said that with respect to Nigeria and the work of SERAP, it is disheartening that this cankerworm was noticed at the foundational level of education in Nigeria - the basic education relating to the Nigerian children - their first nine years of education where massive embezzlement and misappropriation of funds running into billions of naira took place.</p>
<p style="text-align: justify;">
	According to him, the ECOWAS court has stated clearly that there should be no place for corruption in the education sector. The was contained in the judgement delivered in support of the right of the Nigerian Child to free education at the Basic level. The Court further ordered the Federal Government to make funds available to offset what had been frittered away by the looting of Universal Basic Education Fund.</p>
<p style="text-align: justify;">
	In line with this, SERAP has mounted campaign in conjunction with a lot of other public spirited bodies both nationally and internationally - International Commission of Jurists, Interights, Nigerian Union of Teachers and Nigerian Guild of Editors by petition to the President of Nigeria and the Attorney-General of Federation. This has not yielded any positive result almost two (2) years after the Petition was sent and three (3) years after the Judgement was delivered.&rdquo;</p>
<p style="text-align: justify;">
	Mumuni added that since the free education judgment was delivered, more than 5 million Nigerian children of school age still roamed the streets with no access to primary education and 115 million Nigerian adults are still illiterates. He averred that &quot;we cannot wait forever to secure the implementation of the ECOWAS Court right to education judgment&quot;. We are therefore calling on the Authority of Heads of State and Government of ECOWAS to without further delay impose economic and political sanctions against the Nigerian government, pursuant to article 77(1) of the ECOWAS Treaty for non-implementation of the judgment on the right to education for every Nigerian child.</p>
<p style="text-align: justify;">
	The Global Corruption Report: Education shines light on the multiple manifestations of corrupt and unethical practices in education, be they the embezzlement of national education funds in Kenya, the selling of fake diplomas in Niger, teacher absenteeism in Cameroon, or sexual harassment by male lecturers in Nigeria.</p>
<p style="text-align: justify;">
	According to the report, from improper budget spending and insufficient access to education to poor teaching practices and nepotism, corruption in education is rampant across Africa, robbing millions of young people of their right to knowledge and a decent future. Almost one in three Africans paid bribes to education service last year. According to Transparency International&rsquo;s 2013 Global Corruption Barometer, education is perceived as a very corrupt sector.&rdquo;&nbsp; The report also shows that if not addressed, corruption may even lead to the collapse of a country&rsquo;s entire education system and the waste of scarce public resources.</p>
<p style="text-align: justify;">
	TI and SERAP argued that taking bold steps to prevent the abuse of power, bribery and secret dealings from corroding the educational experience is particularly important not only to keep children in school but also to help them acquire the skills and knowledge that will enable them to contribute to their country&#39;s development.&rdquo;</p>
<p style="text-align: justify;">
	According to Chantal Uwimana, Director of the Sub-Saharan Africa Department at Transparency International, most indicators from the United Nations and other institutions show that Africa is way behind other regions as far as education is concerned. We lack good educational structures and basic infrastructures and there are not enough schools for the growing numbers of children. For those in school, corruption is learnt from a young age and even accepted as a norm for them and society.</p>
<p style="text-align: justify;">
	It should be noted that Article 15(4) of the ECOWAS Treaty made the judgment of the court binding on member states, institutions of the community, individuals and corporate bodies, Article 76 (2) provided for the finality of the decision of the Court. Also, Article 19(2) of the 1991 Protocol provided that the decisions of the court should be final and immediately enforceable. The court could also refuse to entertain any application brought by the offending member state until such a state enforces the court&rsquo;s decision.</p>
