<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The School of Nursing, University College Hospital has released a notice inviting application from suitably qualified candidates for admission into the above School in October 2013.&nbsp;</p>
<p>
	The General Entrance Examination Committee in University College Hospital will conduct the entrance examination on 9th March, 2013.&nbsp; Only candidates who attain acceptable standard at the examination will be considered for selection following interview.</p>
<p>
	The course is for a period of three years, after which candidates will be presented for the School and Nursing and Midwifery Council of Nigeria Final Examination to qualify as Registered Nurses [RN].</p>
<p>
	<strong>Admission Requirements:</strong></p>
<p>
	Interested candidates should be of age 17 &frac12; years and above and&nbsp; should possess the West African Senior Secondary School Certificate or its equivalent with credits in at least 5 subjects which must include English Language, Mathematics, Biology, Chemistry and Physics.&nbsp; Only individuals with required credit passes at not more than two sittings from the same examination body may apply.</p>
<p>
	<strong>Method of Application:</strong></p>
<p>
	Candidates should request for application forms from the 3rd December,&nbsp;2012 from the School of Nursing, UCH, Ibadan with:</p>
<p>
	[i]&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Non &ndash; refundable fee of Five Thousand Naira only [N5, 000.00] payable to First&nbsp;Bank Nig. PLC, Account Number 2016771873 and Account Name Admission Form. The First Bank Teller will thereafter be converted to UCH receipt in the Accounts Department prior to purchase of form in the school.</p>
<p>
	[ii]&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Two stamped self-addresses envelopes measuring 22cm x 10cm.</p>
<p>
	[iii]&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Three recent passport photographs [colored but white background only].</p>
<p>
	<strong>No application letter is required.</strong></p>
<p>
	Completed application forms must reach the Principal of the School not later than Friday 22nd February, 2013.</p>
<p>
	<strong>The entrance examination comes up on Saturday 9th March, 2013.</strong></p>
<p>
	<strong>Note: </strong>The School stated in the release that neither the School nor the committee will enter in correspondence with any unsuccessful candidate.</p>
