<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The Association of Professional Bodies of Nigeria (APBN) last week in Lagos decried what it called the marginalisation of Nigerian professionals in the nation&rsquo;s development.</p>
<p>
	It noted that no economy could develop on a sustainable basis without the involvement of professionals.</p>
<p>
	&ldquo;Without the professionals the economy cannot develop as it is done by the Asian Tigers including Hong Kong, South Korea, Singapore and Taiwan.</p>
<p>
	&ldquo;The association views with concern the plight of Nigerian professionals in the emerging economy of our great country.</p>
<p>
	&ldquo;The new trend of giving preference to foreign professionals in our developmental programmes has resulted in strangulation of both the Nigerian professionals and the economy,&rdquo; APBN said in a communiqu&eacute; obtained by the News Agency of Nigeria (NAN).</p>
<p>
	The communiqu&eacute; signed by Mr. Bala Bawa Ka&rsquo;oje and Mrs. Ramatu Aliyu, APBN&rsquo;s President and Secretary General, was released after the association&rsquo;s first board meeting in 2013.</p>
<p>
	The association lamented that experienced professionals in the country were finding it difficult to get jobs.</p>
<p>
	&ldquo;When they are given jobs, they experience a lot of difficulty getting paid for their services.</p>
<p>
	&ldquo;This is worrisome and detrimental to the progress of the country, especially with respect to utilising the nation&rsquo;s human capital.&rsquo;&rsquo;</p>
<p>
	APBN noted that the development had led to the closure of many businesses, thereby reducing employment opportunities for youths with professional education and skills.</p>
<p>
	It said that the situation had led to the relocation of the nation&rsquo;s professionals to other countries.</p>
<p>
	The body appealed to federal and state governments as well as other public institutions to patronise the nation&rsquo;s professionals.</p>
<p>
	&ldquo;This is the route other nations have followed for sustainable development and prosperity.&rdquo;</p>
