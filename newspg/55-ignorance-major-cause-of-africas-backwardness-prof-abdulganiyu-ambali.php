<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The Vice-Chancellor, University of Ilorin, Prof. AbdulGaniyu Ambali has traced the root cause of Africa&#39;s developmental challenges to ignorance. Prof. Ambali stated this in Ghana recently while delivering his address titled &ldquo;SOLIDARITY FOREVER&rdquo;, during the opening session of the Third Joint International Conference of the Universities of Ilorin and Cape Coast at the School of Medical Sciences Auditorium, University of Cape Coast, Ghana.</p>
<p style="text-align: justify;">
	He went further that ignorance remains the major root cause of the various challenges confronting African nations; other problems like poor leadership, corruption, unemployment, diseases, and violence are products of ignorance. With more functional education, which according to him, is the foundation of development, Africans nations shall overcome these challenges and become great. The don likened the difference between the educated and uneducated persons to the difference between darkness and light. He noted that the conference was important because &lsquo;&rsquo;when we discuss various issues that surround &#39;Education for Sustainable Development in Africa&#39;, we are emphasizing the need for Africa to take education seriously because our education is directly proportional to our development&rdquo;.</p>
<p style="text-align: justify;">
	He lauded the conference jointly organised by the two universities and described it as part of the fulfilment of the dream of the first President of Ghana, Dr Kwame Nkrumah, who established the University of Cape Coast. He added that President Nkrumah envisioned an Africa with a common agenda that is opposed to the colonial concept of Africa&#39;s vast lands as &ldquo;Terra Incognita&rdquo; or &ldquo;the unknown land&rdquo;. He assured the participants that the solidarity between the two universities would continue with collaborative activities geared towards the strengthening of the continent.</p>
<p style="text-align: justify;">
	The Vice-Chancellor stressed the need to take the collaboration further by carrying other African universities along beginning with the implementation of the consortium of six African universities &ldquo;U6&rdquo; agreement endorsed at the University of Ilorin. He also expressed hope that beyond the &ldquo;U6&rdquo;, more universities in African could still be carried along to accelerate development.</p>
<p style="text-align: justify;">
	In his remarks, the Nigerian High Commissioner to Ghana, Ademola Oluseyi Onafowokan also commended the two universities for collaborations. He said that the choice of the theme &ldquo;cannot be more apt than at this time when Africa is confronting challenges in meeting the Millennium Development Goals in which the reduction of poverty and infant mortality, and water sanitation issues occupies a central position&rdquo;. He went further to say that education for sustainable development allows every human being to acquire the knowledge, skills, attitudes, and values necessary to shape a sustainable future. The High commissioner added that for such education to be effective, the entire educational system has to be changed to accommodate the new realities of our time.</p>
