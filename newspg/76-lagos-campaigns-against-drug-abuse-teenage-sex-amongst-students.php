	Oladunjoye, who was also given an award for exemplary service, dedicated it to all children in Lagos State who have deemed it fit to abstain from drug abuse, saying that she was humbled to be inducted as the matron of the drug free club. She promised to work hard to make sure that all students in Lagos State Schools stay away from drugs and commended the Chaplains of the drug free clubs for all their efforts to sensitize students against drug abuse.</p>
<p style="text-align: justify;">
	Earlier, the State Commandant/Coordinator-General, Drug Free Club, Mr. &nbsp;D. A. Babalola recalled that the drug free club was established to eradicate drug abuse among students in all public Secondary Schools in order to redefine education and change students&rsquo; behavior and character positively.</p>
