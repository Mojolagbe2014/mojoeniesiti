	Lagos State Government said it has put in place public enlightenment programmes to sensitize students against the twin-evil of drug abuse and teenage sex. Commissioner for Education, Mrs. Olayinka Oladunjoye, who stated this in Lagos while being inducted as the first Matron of the Drug Free Clubs in public Secondary Schools in the state &nbsp;stated that &nbsp;the inherent dangers associated with drug abuse and teenage sex &nbsp;are higher. She said the need to eradicate them in all schools in Lagos in particular and Nigeria in general cannot be over emphasized.&nbsp;&nbsp; &nbsp;</p>
<p style="text-align: justify;">
	According to Oladunjoye, children are the future of the nation hence everything possible must be done to ensure that they are protected against all evils so that their tomorrow is secured. She assured that government would synergize and cement the relationship as well as d support the drug free clubs in its campaign against drug abuse.</p>
