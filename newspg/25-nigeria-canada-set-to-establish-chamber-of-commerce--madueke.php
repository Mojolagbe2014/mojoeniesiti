<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	Nigeria and Canada are set to establish a chamber of commerce to ensure the implementation of the outcome of the Nigeria-Canada Investment Conference (NCIC) in May, Ambassador Ojo Madueke said. &nbsp;</p>
<p>
	Madueke, the Nigerian High Commissioner to Canada, said this while briefing newsmen in Abuja on Thursday. &nbsp;</p>
<p>
	He said that the Canada-Nigeria Chamber of Commerce would promote trade benefits between both countries. &nbsp; ``To ensure that this would not just be another talk shop and that there is momentum after the conference and results that can be monitored, measured and sustained, the mission led the drive to get a new Canada-Nigeria Chamber of Commerce. &nbsp; ``</p>
<p>
	The chamber of commerce, which we have asked, Mr Peter Kieran, the CEO of CPCS, is driving this whole process ably supported by Dr Isa Odidi. &nbsp; ``We want it to be like no other Chamber of Commerce that has been established that deals with investment in Nigeria outside Nigeria. ``What we have done is to ensure that the cockpit, so to speak, of this Chamber of Commerce is manned by companies that have huge capacities and interests; we want those who really have strong stake in terms of the huge traffic, potentially speaking, between Nigeria and Canada in trade areas. &nbsp;</p>
<p>
	``Getting the Chamber of Commerce is a way of creating the ties that bind between Nigeria and Canada on a more permanent basis. &rsquo;&rsquo; Madueke said the Nigeria-Canada Investment Conference, which would take place from May 2 to May 3 in Toronto, Canada, would focus on sectors that would promote job creation and economic development. &nbsp;</p>
<p>
	``We have a dynamic labour force of about 100 million young people; the biggest asset Nigeria has is its labour force and if this labour force is given the right training, we would take over the global economy. &nbsp;</p>
<p>
	``This conference would create opportunities for an enabling environment for youth productivity and development to thrive, thus enabling skills acquisition and economic diversity and growth. &rdquo; &nbsp;</p>
<p>
	He listed mining, oil and gas, education, banking, information technology, health and infrastructure as areas of investments. &nbsp; Also speaking, Canadian High Commissioner to Nigeria, Mr Chris Cooter, expressed the readiness of the Canadian government to partner with Nigeria in the areas of youth development, agriculture and film industry. &nbsp;</p>
<p>
	Cooter said that the conference would create opportunities for Canada to invest in &ldquo;Nigeria&rsquo;s future&rdquo;, adding that it would also create a positive perception of Nigeria and the African continent to the international community. &nbsp;</p>
<p>
	``This is really setting a path for Nigeria&rsquo;s engagement with the rest of the world; an event such as this is historic in our bilateral relation and beyond that, it speaks to the change in the perception of the whole world towards Africa and towards Nigeria in particular.</p>
<p>
	&nbsp; ``We want this relationship not just to be a one-off event; we want continuity; we want to create relationships between business, people and institutions. &nbsp; ``We see the future with you and we need to diversify our trade relations and the big new developing countries like Nigeria are of particular priority to the government of Canada.&rsquo;</p>
