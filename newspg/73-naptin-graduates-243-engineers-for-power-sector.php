<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The National Power Training Institute of Nigeria (NAPTIN) in Abuja has graduated 243 engineers in the three key areas of the power sector. Mr Reuben Okeke, the Director General of NAPTIN said the graduates studied programmes in three critical areas of Generation, Transmission, and Distribution as the nation was striving to boost the improvement of power supply in the country.</p>
<p style="text-align: justify;">
	According to him, the institute graduated 92 students in Generation Engineering, 79 in Transmission and 72 engineers in Distribution.</p>
<p style="text-align: justify;">
	Okeke explained that this was the first set of graduate engineers who had undergone a rigorous 12 month skills development programme in the three areas of the electricity production chain.</p>
<p style="text-align: justify;">
	He said that NAPTIN Graduate Skills Development Programme (NGSDP) which started in Sept. 2012 received over 1,530 applications out of which only 243 applicants were eventually admitted by the institute.</p>
<p>
	Okeke expressed his appreciation to nine state governments which sponsored some of the candidates in the programme. He listed these state governments as those of Anambra, Bornu, Ebonyi, Kano, Katsina, Plateau, Rivers, Sokoto and Yobe states.</p>
<p>
	He explained further&nbsp; that the training was to ensure that privatisation of the power sector was not just to handover assets to investors but to a crop of trained professionals.</p>
<p>
	He said that NAPTIN was at present collaborating with National Universities Commission to ensure it strengthens its training capabilities by partnering with some Nigerian universities.</p>
<p>
	He added that the German Government under the Nigeria- German Energy Support Programme would soon complete the development of a 25 KVA Wind / Solar Hybrid Renewable Demonstration plant at Kainji Training Centre.</p>
<p>
	Okeke said that U.S. Trade and Development Agency recently granted the sum of 16,000 dollars to NAPTIN to study power loss reduction technologies.He said that the institute was ready to share the outcome of that study with the new core owners of electricity assets in the country.</p>
<p>
	The Director General expressed his appreciation to the President Goodluck Jonathan for his encouragement and financial support to the organisation.</p>
<p>
	Speaking at the occasion, the Minister of Power, Prof. Chinedu Nebo, said that capacity training was the key area he wanted to develop in the power industry. According to the minister ``Without engineers, things like motor cars, electricity, telecommunication and even medical doctors will not do their work very well&rsquo;&rsquo;.</p>
<p>
	Earlier, the Permanent Secretary in the Ministry, Dr Godknows Igali, said that the graduating students were the change agents in the country. He said that the ministry and the Nigerian Electricity Regulatory Commission only make policies for the sector while the engineers were the ones that implement these policies. Igali called for more sponsors for this kind of programmes.</p>
<p>
	Mr Patrick Ikhariele, the Chairman, House Committee on Power, said that the National Assembly was still ready to ensure that the NAPTIN got the required funds to train its students.</p>
<p>
	Mr Dagogo Jack, the Chairman, Presidential Taskforce on Power, said that the organisation would continue to assist NAPTIN in executing its mandate to ensure that the nation&rsquo;s power sector stabilised.</p>
<p>
	Mr Ilobe Mba and Mr Nura Mohammed, who scored 83 per cent and 82 per cent in Electrical Engineering and Mechanical Engineering, respectively received awards as the best graduating students</p>
