	The Acting Executive Chairman of the Federal Inland Revenue Service (FIRS) Kabir Mashi, has called on members of the Association of National Accountants of Nigeria (ANAN) to ensure due diligence and professionalism in their carrier.<br />
	<br />
	Mashi, in a statement signed by the Director in the Communications Department of the Service, Emmanuel Obeta, advised the accounting professionals not to only seek members but also ensure appropriate training programmes that would enhance their skill as a strategic option of positioning the members of the association to play leading roles in the accounting profession.<br />
	<br />
	While pledging the FIRS&rsquo; continued support in the area of training both local and international for more than 350 ANAN members who are staff of the revenue agency, Mashi, who is also a member of the association, urged the leaders to encourage other staff to join the professional body as part of effort to deepen knowledge of Nigerian accountants.<br />
	<br />
	This approach, he noted, would assist the Service in its core mandate of tax revenue collection and accounting for the country.<br />
	<br />
	Earlier in his remarks, the leader of the delegation and Chairman of the branch of ANAN, Rakiya Yusuf, who commended Mashi for sustaining reforms in the Nigeria tax system, solicited support for ANAN members and the association.<br />
	<br />
	She noted that the sustenance of the nation&rsquo;s tax reform had resulted in increase tax revenue collection; focus on taxpayers services and gradual shift from a sole dependence on oil revenue.<br />
	<br />
	ANAN was founded in 1979 and Chartered by Act 76 of 1993. The association is also a member of international bodies like the International Federation of Accountants (IFAC), Association of Accountancy Bodies in West Africa (ABWA) and Pan African Federation of Accountants (PAFA).</p>
