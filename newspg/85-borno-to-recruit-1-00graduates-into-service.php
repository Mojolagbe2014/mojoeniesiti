The governor explained that the essence is to fill the existing gap created at the middle cadre level in the service due retirements and deaths, adding that the recruitment would hold in the first quarter of the year.<br />
<br />
A onetime president of the Nigeria Labour Congress (NLC), Comrade Ali Chiroma, led the delegation. Chiroma commended the governor for his developmental projects in spite of the prevailing security challenges facing the state.<br />
<br />
He appealed to the government to ensure regular release of overhead to state ministries and departments to enable them carry out their functions.<br />
