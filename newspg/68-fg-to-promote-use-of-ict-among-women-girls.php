<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The Federal Government is set to launch three gender initiatives to promote the use of Information Communication Technology (ICT) among women and girls, Minister of Communication Technology, Omobola Johnson said. She made the disclosure on Tuesday at a news conference in Abuja.</p>
<p style="text-align: justify;">
	The minister, who said ICT would also be used to generate new employment opportunities, added that ``the initiative will create an army of knowledgeable girls with potential to contribute to the socio-economic development of the country.&rsquo;&rsquo;</p>
<p style="text-align: justify;">
	While saying ICT would help to develop women and girls to be efficient and effective in their jobs, careers and businesses, she announced that the ministry is set to launch three different initiatives that will empower women and girls.</p>
<p style="text-align: justify;">
	According to the minister, the initiatives are:</p>
<ul>
	<li style="text-align: justify;">
		Smart-Woman Nigeria Initiative,</li>
	<li style="text-align: justify;">
		Hauwei/MCT ICT training for girls and</li>
	<li style="text-align: justify;">
		ICT Girls Club.</li>
</ul>
<p style="text-align: justify;">
	The smart-woman project&rsquo;s target is to have 20,000 women using and receiving contents from smart-woman Nigeria by the end of the first year, with the figure rising to 60,000 by the end of second year and 100,000 by the third year.</p>
<p style="text-align: justify;">
	She said the objectives of the smart-woman project were to promote the development of women and to provide accurate information that would be relevant to their needs. She added that the project would allow women to connect to one another, share knowledge and learn from each other.</p>
<p style="text-align: justify;">
	According to the minister, the project would be delivered in two phases; phase one was to build up community of women that would subscribe to start-woman service through digital device like phones, tablet and PC.</p>
<p style="text-align: justify;">
	Johnson said the women would pay a subscription fee of between N160 and N320 per month to receive messages on career tips to parenting, health, finance and leisure.</p>
<p style="text-align: justify;">
	The minister noted that the second phase would be extended to more beneficiaries who would receive specific contents via text messages free of charge and such content would include life skills, money management, health and security issues.</p>
<p style="text-align: justify;">
	Under this phase, 1000 unemployed girls would also be trained in ICT within a year in specific crucial ICT skills necessary for their empowerment and economic well-being.</p>
<p style="text-align: justify;">
	According to her, the training will include introduction to telecommunication networks, introduction to transmission network and GSM, among others.</p>
<p style="text-align: justify;">
	The training would commence on November &nbsp;4, and the top 200 students based on performance would be given further training to become Huawei certified network associates, she said.</p>
<p style="text-align: justify;">
	She said the ICT Girls Club initiative was perceived as a viable platform to create an enabling environment to encourage young girls to embrace ICT. The goal is to ensure that more girls embrace and adopt ICT, as such clubs will be set up in private and public schools that already have computer labs with Internet access in the six geo political zones of the country.</p>
<p style="text-align: justify;">
	Five ministries had signed Memorandum of Understanding (MoU) with the Ministry of Women Affairs and Ministry of Finance to develop the potential of women in the country.&rsquo;&rsquo;</p>
<p style="text-align: justify;">
	She listed the ministries to include Communications Technology, Agriculture, Water Resource, Health and Works. (NAN)</p>
