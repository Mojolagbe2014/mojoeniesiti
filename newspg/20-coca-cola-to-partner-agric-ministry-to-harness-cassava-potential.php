<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	The Ministry of Agriculture and Rural Development and the Coca-Cola Group on March 30 , 2013&nbsp; begun discussions on&nbsp;partnership to harness some cassava&nbsp;potential.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	Kelvin Balogun, the President, Coca-Cola (Central, East and West Africa), told the Minister, Dr Akinwumi Adesina, during a courtesy visit to his office that the group was motivated by his&nbsp;policies since he assumed office.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	Balogun said that the&nbsp;group was interested in two of the policies -&nbsp;the local development of high citrus starch syrup and production of fruit juice concentrate from cassava.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	According to him, these products are currently&nbsp;being imported from Thailand.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	Balogun stressed that the group was committed to the sourcing of local raw materials in&nbsp;this line of business.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	&ldquo;These products are being imported from Thailand and on continual basis, we have to&nbsp;look for large concentrate of raw materials for these products,&rdquo; he said. &ldquo;We are extremely committed to this course and our ultimate goal is to produce locally made juice and give access to global procurement.&rsquo;&rsquo;</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	In his remarks, Adesina explained that the partnership was important because it would help add value to what the country produced locally.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	He noted that the country spent N1 billion said that the country spent one billion naira annually on the importation of&nbsp;fruit juice and concentrate, a situation he said was not good for the economy. He added that the country also spent a lot of money importing sugar.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	&ldquo;We have to look at other substitutes,&rdquo; he said. &ldquo;Cassava is a miracle crop and it can be transformed into so many things. It is a highly versatile crop. We can replace the 150 tonnes of citrus and juice concentrate that we import into this country through some of the potential from cassava.&rdquo;</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	He said that the ministry was working with a company on two projects in Kwara and Kogi, to produce high citrus starch syrup and fruit juice concentrate from cassava.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	According to him, the company has&nbsp;invested in 75,000-tonne plant and also on a 15,000 tonnes to produce juice sweetener.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	The minister stressed that the federal government was committed to turning cassava into a tradeable commodity.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	He further said that&nbsp;Flour Mills Nigeria Ltd. was also investing in sweetener and starch plants.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	Adesina gave an assurance&nbsp;that the federal government would provide the needed&nbsp;support to efforts directed at import substitution.</p>
<p style="font-size: 1.2em; line-height: 1.7em; margin: 0px 0px 1.1em; color: rgb(51, 51, 51); font-family: Georgia, 'Times New Roman', serif;">
	He said that all areas where these citrus plants would be situated would have the presence of Staple Crop Processing Zones (SCPZ).</p>
