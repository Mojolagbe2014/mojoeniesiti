<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	Against the background of the announcement by President Goodluck Jonathan on Tuesday 23rd April 2013 that the Federal Government would still go ahead to increase the pump price of petrol, the National Association of Chamber of Commerce Industry Mines and Agriculture (NACCIMA) has warned the Jonathan-led government to halt the move in the interest of Nigerians.</p>
<p>
	The latest move to discourage the Federal Government from the decision came from the National President of NACCIMA Mr.Herbert Ajayi at the weekend. Specifically, Ajayi disclosed that NACCIMA had, in January, warned the Federal Government not to tinker with the idea of increasing fuel pump price, saying such move would further impoverish Nigerians.</p>
<p>
	The NACCIMA Chief regretted that the decision would further increase the cost of doing business while adding more to the many troubles that manufacturers are confronted with, hence the need for government to suspend the action plan. &ldquo;At this time that the transformation trains is moving towards the realization of tangible achievements, it is not advisable that Jonathan is trying to compound the woes of Nigerians and manufacturers. That is the angle I feel the public is worried about,&rdquo; he said. Recall that President Jonathan had at the Nigerian Summit on Tuesday said the Federal Government would still remove fuel subsidy. But he said the government would first discuss the proposal with Nigerians before removing the subsidy.</p>
<p>
	The President said at the Nigeria Summit which held in Lagos: &ldquo;We cannot continue to waste resources meant for a greater number of Nigerians to subsidize the affluent middle class, who are the main beneficiaries (of fuel subsidy). &ldquo;We believe that as we progress, government is going to continue to enlighten Nigerians on the need to remove fuel subsidy, he maintained,&rdquo; he added.</p>
<p>
	The Federal Government had announced the total removal of subsidy on petrol on January 1, 2012 without prior announcement, raising the price of a litre of the commodity to N141 from N65. The removal was, however, vehemently rejected by the citizens, who staged week-long mass demonstrations in major cities of the country, forcing the government to partially remove the subsidy and reduce petrol price from N141 to N97 a litre.</p>
<p>
	According to the Petroleum Product Pricing Regulatory Agency&rsquo;s pricing template, the landing cost of a litre of petrol is currently N131.10, with total distribution margins of N15.49, thus bringing the total cost to N146.59. This means that if the government eventually removes the subsidy, Nigerians will be paying a minimum of N146.59 per litre of petrol at filling stations.</p>
