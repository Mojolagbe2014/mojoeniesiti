<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	In the quest to bring the benefit of management accountancy nearer students and to enhance business decision-making and performance in Nigerian workers, the Chartered Institute of Management Accountants (CIMA) has restated its commitment to collaborate with higher institutions in Nigeria to improve the quality of professionals in Accountancy.</p>
<p>
	Mrs. Samantha Louis, the Regional Director for Africa said &ldquo;CIMA is always available to collaborate with schools in whichever way possible to improve the quality of the professionals as we continue to sponsor research works from higher institutions in Nigeria and all over the world.&rdquo;</p>
<p>
	Louis noted that CIMA which was founded 93 years ago berthed in Nigeria a year ago to quell the yearning of Nigerians to have an international certificate that will broaden their global job opportunities.</p>
<p>
	She said that the road to CIMA membership is open to all, regardless of educational background adding that &ldquo;Our flexible entry policy enables anyone with a passion to succeed in business the chance to study with CIMA.&rdquo;</p>
<p>
	According to her, &ldquo;if you have a relevant degree, CIMA&rsquo;s carefully defined exemptions policy will allow you to start your CIMA studies at the right point for you.&rdquo; This will enable you to progress more quickly through the qualification.</p>
<p>
	She explained further that if you are new to business or accounting studies, you will first need to work towards gaining the CIMA Certificate in Business Accounting which will enable you to move on to the CIMA professional qualification.</p>
<p>
	The Regional Director who disclosed that CIMA has a good relationship with the existing professional bodies in the country said, CIMA&rsquo;s accelerated route has been extended to the members of ICAN and members of International Federation of Accountants, IFAC. The route, according to her, grants them 11 exemptions out of 15 papers, leaving them to do just four papers after an assessment.</p>
<p>
	She said; &ldquo;CIMA&rsquo;s mission is to help the world economy grow. Our ability to operate in Nigeria since the last one year reveals that Nigeria is a country which has the potential to grow faster. Though already growing, but to actualize Vision 20-2020, we must contribute our ability and our expertise in capacity-building of financially qualified business leaders that will grow the economy of Nigeria.</p>
<p>
	She stated further that CIMA&rsquo;s joint venture with American Institute of Certified Public Accountant has opened more international opportunities for CIMA members who now have CGMA (Chartered Global Management Accountant) designation in addition to ACMA or FCMA. This has opened more opportunities for CIMA members in America.</p>
<p>
	She however noted that CIMA is not a profit-making organization, stressing that any money generated is invested in the members.</p>
