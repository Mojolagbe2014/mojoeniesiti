<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	More than 200 youths to be drawn from Lagos, Ogun and Abuja are to undergo training under the German dual vocational education partnership with Nigeria.</p>
<p style="text-align: justify;">
	The dual vocational training partnership is a poverty alleviating support through sustainable economic development of partner country - Nigeria and German Ministry of Economic Cooperation and Development.</p>
<p style="text-align: justify;">
	The youths would be trained in Industrial mechanic maintenance and technical facility management; office assistants and project management; electrics, electronic automation and drive engineering technique for the manufacturing industry Coordinator Dual Vocational Training Partnership with Nigeria,.</p>
<p style="text-align: justify;">
	&nbsp;Engr. Kehinde Stephen Awoyele speaking at a stakeholders meeting said the project will Increase level of qualifications and employability of young men and women in selected vocational professions and locations for them to find jobs and receive income which guarantees their existence.</p>
<p style="text-align: justify;">
	Awoyele, who lamented insufficient involvement of the private sector in the development of curricula and training, said relevant occupations have been identified and existing curricula have been further developed (adapted) according to dual vocational training approach.</p>
<p style="text-align: justify;">
	According to him, we are not bringing the system of Germany to Nigeria, but we are adapting it to our peculiarities to meet our needs. He added that the project would not engage any youth if we are not sure that the company training the youths would employ them.</p>
<p style="text-align: justify;">
	He explained that the training is demand driven; the organization brings people, majorly youths who are ready to be given employment, there has to be an employer who has identified lack of skilled worker in an area and is ready to train one.</p>
<p style="text-align: justify;">
	He said quality and services of companies taking part in training would increases as a consequence of employing people who are better qualified in specific fields.</p>
<p style="text-align: justify;">
	He noted that the project focuses on the improvement of vocational education quality within Nigerian enterprises by a better combination of theory and practice.</p>
