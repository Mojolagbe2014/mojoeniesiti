<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	Inadequate leadership training is one of the challenges affecting development in Nigeria, the Vice-Chancellor of Ekiti State University, Prof. Patrick Oladipo Aina has said. He made the observation during a workshop for Students Union leaders of the institution held at the Odua hall of the University.</p>
<p style="text-align: justify;">
	Represented by the Deputy-Vice-Chancellor (Academic), Prof. Olugbenga Aribisala, the Vice-Chancellor expressed dismay over the absence of training and retraining programmes in several sectors of the nation.</p>
<p style="text-align: justify;">
	He said that the need for workshops concerning Students&rsquo; Unionism in Nigerian tertiary institutions could not be over-emphasized as there was the need to change the orientation of students from the old fashion of &lsquo;Aluta Continua&rsquo; to something progressive in order to stop the unnecessary demonstrations in Nigerian Universities.</p>
<p style="text-align: justify;">
	&ldquo;Rampaging or violent demonstrations are archaic, students should embrace peaceful demonstrations and dialogue to resolve issues&rdquo;, the Vice-Chancellor asserted.</p>
<p style="text-align: justify;">
	During the workshop, the Dean of the School of Post-Graduate Studies, Prof. Eddy Olanipekun admonished the students to change with time as violent demonstrations are no longer in vogue. In his address centred on Students&rsquo; Unionism in the 21st Century, he advised students to always adopt productive dialogue rather than delving into riotous protests which should not be a hallmark of a world-class institution such as Ekiti State University.</p>
<p style="text-align: justify;">
	Prof. Olanipekun however noted that students could only embark on protests when other avenues of dialogue have been exhausted.</p>
