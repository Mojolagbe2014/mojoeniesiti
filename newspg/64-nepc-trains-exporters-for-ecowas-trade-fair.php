	The Nigerian Export Promotion Council says it has trained exporters in the country on the modalities of exhibition ahead of the 7th ECOWAS Trade Fair in Ghana.</p>
<p style="text-align: justify;">
	This was made known at a one-day trade show preparation workshop was organised by the council in collaboration with the United States Agency for International Development and Nigeria Expanded Trade and Transport, in Lagos on Thursday.</p>
<p style="text-align: justify;">
	NEPC Acting Executive Director, Mr. Aliyu Lawal, said the training was important to ensure that Nigerian exporters achieved their purpose for participating in the exhibition.</p>
<p style="text-align: justify;">
	Aliyu, who was represented by the Deputy Director, NEPC Marketing Development, Mr. Mathew Iranloye, said, &ldquo;The purpose of this programme is to equip and train participating exporters at the 7th ECOWAS trade fair scheduled to hold in Accra on how to maximise the benefit of attending trade shows.</p>
<p style="text-align: justify;">
	According to NEPC, international trade rules are dynamic and constantly changing and it is important for the wary exporter to continually find ways of developing himself to be able to stay abreast of developments in the sector.</p>
<p style="text-align: justify;">
	He opined that trade shows can be great tools for marketing development nothing that without proper planning and follow-up a trade shows has little measurable return on investment.</p>
<p style="text-align: justify;">
	Acting Zonal Controller, NEPC, Lagos, Mrs. Evelyn Obidike, added that the best way for companies to succeed in export business is to have a carefully designed and competently managed exporting programme such as an international trade fair.</p>
