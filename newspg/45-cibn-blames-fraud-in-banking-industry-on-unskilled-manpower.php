	Chartered Institute of Bankers of Nigeria, CIBN, has attributed the prevalence of fraud in the banking industry to non-professionals occupying sensitive positions in banks.</p>
<p style="margin: 0px 0px 1em; color: rgb(0, 0, 0); font-family: arial;">
	Deji Olanrewaju, a member of the governing council of the Institute said this in a press briefing organised by its Lagos Chapter new executive committee, adding that there is still the paucity of professional bankers in the country which is a major factor for lapses in the industry.</p>
<p style="margin: 0px 0px 1em; color: rgb(0, 0, 0); font-family: arial;">
	He said &ldquo;It is instructive to note that when we are talking about professional bankers, we are less than 4,500 that are professionally qualified as core bankers in this country. Out of these, some of us are in the academic world, some are already retired. &ldquo; So, if you are talking about professionalism in the banking industry which has been our focal point, these problems arose as a result of people who are not professionals occupying sensitive positions.</p>
<p style="margin: 0px 0px 1em; color: rgb(0, 0, 0); font-family: arial;">
	According to him, there are certain key areas that should not be handled by non professionals. But unfortunately, over the years, just a small number of people that have been working in the banking industry are professionals. That is why we taking concerted effort now to make sure that our rules are amended so that unless you are a chartered member of this institute, you cannot even work in a bank. &ldquo;So, until we are able to drive this and government is able to support us, we may not be able to be answerable to all those people who commit fraud in the banking industry,&rdquo; he said.</p>
<p style="margin: 0px 0px 1em; color: rgb(0, 0, 0); font-family: arial;">
	To tackle this situation, the Institute said it is organising an international conference in Dubai UAE for the Regulators and Operators in the financial services industry to a view to evolving best practices for implementation of Basel 11 and Base111 in Nigeria. The new Chairman, CIBN Lagos Chapter, Mr. Bolade Agbola said this is part of his six point agenda aimed at instilling sanity in the banking industry.</p>
<p style="margin: 0px 0px 1em; color: rgb(0, 0, 0); font-family: arial;">
	He said the Bank of International Settlement (BIS), the global body evolving rules for risk management had been developing new rules and practices to forestall reoccurrence of the global financial crisis that had put the world economies in prostrate state since 2007. &ldquo;We are living witnesses of how bad an economy could go notwithstanding the so called blessings of oil windfall revenue if there is problem in the financial system,&rdquo; he said.</p>
