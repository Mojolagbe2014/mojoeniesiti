<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	Sen. Anyim Pius Anyim, the Secretary to the Government of the Federation, last week inaugurated the National Partnership Committee of Government and Private Sector on Technical and Vocational &nbsp;Education and Training (TVET).</p>
<p>
	The 18-member committee has membership from government and NGOs, with Alhaji Aliko Dangote, a business tycoon, as chairman and Dr Masud Kazaure, the Executive Secretary of NBTE, as secretary.</p>
<p>
	Inaugurating the committee, Anyim stressed the need for the country to develop the right skills and competency to support industrial development.</p>
<p>
	&ldquo;Such skills have to be made available for the increased manpower and industrial growth needed to address the challenges of unemployment.</p>
<p>
	&ldquo;It is for this reason that Mr President is desirous of inaugurating a Partnership Committee between the Government and private sector to improve TVET,&rsquo;&rsquo; he said.</p>
<p>
	Anyim said the committee would enable the government and private sector to work in harmony to design relevant programmes of education in TVET for industrial use and growth.</p>
<p>
	He said it would also enable the country to leverage on the vast competencies within the private sector in developing programmes to enhance skills development.</p>
<p>
	Further, the SGF said the committee is to develop strategy for improving the effectiveness of TVET and conduct labour market survey to determine TVET needs of Nigerian industries.</p>
<p>
	It is also to promote the adoption of approaches from other environments for Nigerian TVET strategy as well as support the implementation of National Vocational Qualifications Framework and the development of National Occupational Standards.</p>
<p>
	The committee will also design initial pilot institutions for government-private sector support in the development of TVET programmes and consider establishment of a time-bound TVET in Nigeria.</p>
<p>
	Anyim urged the members to leave no stone unturned in their quest to meet the objectives, and promised that the Federal Government would provide the necessary support for them to achieve the set goals.</p>
<p>
	The Minister of Education, Prof. Ruqayyatu Rufa&rsquo;I, said the inauguration was part of efforts to create capacity for the education system to produce individuals capable of functioning effectively in diverse sectors of the economy. According to Rufa&rsquo;l, the ministry&rsquo;s 4-Year Strategic Plan has TVET as one of its focal areas.</p>
<p>
	She said &ldquo;It is an area that we must address successfully in our quest to meet the strategic education goals of Nigerian education of access and quality. &ldquo;In seeking to meet these goals, the private sector is essential because of the need to support the dynamic skills requirements of their operations.&rsquo;&rsquo;</p>
<p>
	In his remarks, Alhaji&nbsp;Aliko Dangote said that setting up the committee clearly showed that &ldquo;the government is a listening one and therefore aware of the hindrances to the industrialisation process in Nigeria&rsquo;&rsquo;.</p>
<p>
	Alhaji Dangote said global competitiveness was determined by the availability of skilled manpower. &ldquo;For us to be a global player as a number of government policy promotes, we must start with developing the appropriate human capital.&ldquo; Skilled manpower ensures that organisations are able to innovate and with innovation comes continuous progress.</p>
