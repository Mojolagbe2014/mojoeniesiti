<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	The House of Representatives yesterday urged the Federal Inland Revenue Service (FIRS) to stop further remittance of PAYE tax to the Ministry of Federal Capital Territory (MFCT). It also mandated its Committee on Finance to establish the actual amount that had been remitted to the ministry, and who authourised the remittances.</p>
<p>
	The committee is expected to report to the house within one week. The resolution followed a motion moved by Rep. Babangida Ibrahim (CPC-Katsina), which was unanimously adopted without debate. According to Ibrahim, the only relevant tax authority responsible for the collection of PAYE within FCT is FIRS, as provided in the Personal Income Tax Act (PITA) 1993, as amended.</p>
<p>
	He alleged that between 2009 and&nbsp;2012, FIRS illegally remitted more than N100 billion PAYE fund to MFCT although the FCT was on first line charge. The legislator stressed that the action of the revenue service was affecting the effort of the Federal Government to effectively finance its budget.</p>
<p>
	He further stressed that the continuous violation of constitutional provisions and relevant laws by the executive arm of government on fiscal issues was affecting economic growth of the country. Meanwhile, the House has urged the Joint Health Sector Union to suspend its proposed nationwide strike in the interest of the country.</p>
<p>
	It has therefore mandated the house committee on health to engage all the stakeholders in the proposed strike in discussions and ensure that the strike did not take place. The house also urged the Federal Government to implement, as a matter of urgency, the 2012 collective agreement entered into with the union.</p>
<p>
	The resolution was sequel to a motion moved by Rep. Ndudi Elumelu (PDP-Delta) which was adopted without debate. Elumelu said that any issue affecting the health sector must be addressed with high degree of urgency to avert a breakdown of health services. He said that previous strikes in the sector had recorded huge casualties and led to Nigerians incurring huge expenses by accessing medical attention in private hospitals. He warned that &ldquo;If urgent steps are not taken by this house to avert the looming industrial unrest, the health service delivery system in the country would be terribly affected&quot;.</p>
