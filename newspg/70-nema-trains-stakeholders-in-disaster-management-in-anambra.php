<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The National Emergency Management Agency (NEMA) on Thursday organised a training for stakeholders in disaster management in Anambra to reduce auto crashes.</p>
<p style="text-align: justify;">
	The South East Zonal Coordinator of the agency, Dr Bandele Onimode, said at the training that it was aimed at saving and salvaging accident victims on the highways and to improve people&rsquo;s attitude to disaster management.</p>
<p style="text-align: justify;">
	Onimode, who was represented by Dr Innocent Ezeaku, Assistant Zonal Coordinator, said road crash was one of the commonest disasters in the country, saying: &ldquo;It happens daily and many lives and properties are lost.</p>
<p style="text-align: justify;">
	&ldquo;Therefore, no amount of effort that is put in this regard will be too much,&rsquo;&rsquo; he said.</p>
<p style="text-align: justify;">
	While commending the participants from the police, Nigeria Security and Civil Defence Corps, FRSC and the fire service, he said human capital development as a means of building skills, ideas and concepts were necessary.</p>
<p>
	According to him, the agency is training them as part of measures to ensure that emergency management stakeholders are ready, willing and able to provide professional services to reduce causalities during disasters.</p>
<p>
	``Disaster does not discriminate, therefore, concerted efforts are usually required to assuage its effects.</p>
<p>
	``There is need to sensitise, mobilise and equip ourselves for challenges associated with disaster to further reduce the level of carnage on our highways by 2020,&rdquo; he said.</p>
<p>
	The Executive Secretary of the Anambra Emergency Management Agency, Dr Nwabufo Ijezie, said the training was long overdue because the agencies lacked the necessary funds to train their personnel.</p>
<p>
	He said the poor road network in the country, particularly in the South East, had caused many crashes and had become worrisome.</p>
<p>
	&ldquo;Many lives have been lost and properties worth huge sums of money destroyed,&rsquo;&rsquo; he said.</p>
<p>
	Ijezie urged governments to maintain their roads to reduce the spate of crashes in the country.</p>
<p>
	FRSC&rsquo;s Head of Operation in the state, Mr John Malomo, said the exercise would go a long way to improve the efficiency of the personnel.</p>
<p>
	He lamented the increasing carnage through auto crashes in the country, adding that it would also take the collaborative efforts of all to reduce it.</p>
<p>
	The training, which focused on vehicle extrication and mass causality handling in auto crashes, was a collaboration among the Anambra State Emergency Management Agency, the state fire service and the FRSC.&nbsp; (NAN)</p>
