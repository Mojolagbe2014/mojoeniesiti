<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The Kano State Government has donated a 30-room hostel to the School of Basic and Remedial Studies (SBRS) of Ahmadau Bello University (ABU), Zaria.</p>
<p style="text-align: justify;">
	Gov. Rabi&#39;u Kwankwaso, in a message at the handing over of the structure in Funtua, Katsina State, said the gesture was meant to alleviate shortage of accommodation facing the school.</p>
<p style="text-align: justify;">
	Kwankwaso, who was represented by Alhaji Umar Doguwa, the Commissioner for Higher Education, appealed to state governments in the North, to redouble efforts at providing assistance to the school.</p>
<p style="text-align: justify;">
	He appreciated the contribution of the institution in empowering youth to gain admission into the universities. The governor pointed out that his administration was currently constructing a hostel at Usman Danfodio University, Sokoto while a similar structure would be constructed at the University of Maiduguri.</p>
<p style="text-align: justify;">
	According to the governor, ``our administration at its inception has identified education to be the most crucial aspect of human development and we have committed billions of naira to the sector&quot;. The permanent site of North West University has so far gulped over N4 billion. This, he said,&nbsp; is in addition to the projects undertaken at the temporary site of the university,&quot; he said.</p>
<p style="text-align: justify;">
	Kwankwaso restated the commitment of his government towards building the capacity of the youth, saying, &quot;this is evidenced in the sponsorship of 1003 graduates to undergo masters degree programmes abroad&#39;&#39;.</p>
<p style="text-align: justify;">
	In his speech, Prof. Abdullahi Mustapha, the ABU Vice Chancellor, appreciated the governor for the support, saying that the gesture would enhance the capacity of the institution.</p>
<p style="text-align: justify;">
	Prof. Mustapha said that ``It requires the foresight of a great leader to recognise a crucial need as this, as well as the generosity of a humane person and committed educationist to provide it&quot;. ``</p>
<p style="text-align: justify;">
	Prof. Mustapha praised the governor further saying &quot;Your Excellency, history will judge you as one of the leaders of the later generation who uphold and nurture the noble practice of the noble leaders of the first generation who dedicated their lives for the betterment of our society.&quot;</p>
<p style="text-align: justify;">
	Prof. Mustapha charged the board and management of SBRS to redouble efforts at getting more facilities through resource mobilisation. (NAN</p>
