<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	An international research grant for a tripartite joint research project between the University of Lagos, the University of Sussex and the Kenyatta University in Nairobi, Kenya has recently been awarded by the European Research Council of the European Union for a new international Cultural Studies project.</p>
<p style="text-align: justify;">
	The project, &lsquo;The Cultural Politics of Dirt in Africa 1880 - Present&#39;, will span five years. It has received funding from the European Research Council (ERC), an arm of the European Union (EU).</p>
<p style="text-align: justify;">
	The Deputy Vice-Chancellor (Academic and Research), Professor Babajide Alo, on behalf of the Vice Chancellor of the University of Lagos, Prof. Rahamon Bello, recently signed the consortium agreement with the University of Sussex in the United Kingdom and Kenyatta University in Kenya to inaugurate The Cultural Politics of Dirt in Africa, 1800- Present (DIRTPOL).</p>
<p style="text-align: justify;">
	The five-year tripartite research project and partnership among the institutions would survey African urban life and generate data based on attitudes to and perceptions of &lsquo;dirt&rsquo; in the cities of Lagos and Nairobi. The international multicultural research project is being funded to the tune of 2.2 million Euros and researchers in the team are seeking to understand and document how multiple and conflicting definitions of &lsquo;dirt&rsquo; activate attitudes to and perceptions of dirt among people of similar or different backgrounds.&nbsp;&nbsp;</p>
<p style="text-align: justify;">
	&nbsp;DIRTPOL is an interdisciplinary study of contemporary attitudes to dirt which is expected, among other things, to inform public health policy and practice in Africa, support NGOs with data to drive their field activities and significantly interface the humanities with the social and physical sciences by generating data that will be useful across disciplines to various scholars interested in cultural and environmental issues.</p>
<p style="text-align: justify;">
	&nbsp;DIRTPOL is the brainchild of Prof. Stephanie Newell, Professor of English at the School of English, University of Sussex. Professor Newell is the current president of the African Studies Association of the United Kingdom (ASAUK).</p>
<p style="text-align: justify;">
	The grant winner and Principal Investigator (PI) in the project has a long-standing relationship with the Department of English at the University of Lagos forged from when she was a graduate student at the Centre of West African Studies (CWAS), University of Birmingham. It has been nurtured through many years of research and other collaborations with Professor Akachi Ezeigbo, FNAL based on Professor Newell&rsquo;s interest in West African literature and popular culture.&nbsp; DIRTPOL is a major strand of her new focus on interdisciplinary research aimed at interfacing the humanities with other fields.&nbsp;</p>
<p style="text-align: justify;">
	The research team headed by Professor Stephanie Newell-the designated Principal Investigator of the Project- is drawn from scholars and support staff straddling diverse disciplinary backgrounds such as media and communications, sociology, public health, cultural history and linguistics. The University of Lagos team is headed by a Regional Coordinator (RC), Dr. Patrick Oloko, a Senior Lecturer in the Department of English.&nbsp;</p>
<p style="text-align: justify;">
	Dr. Oloko will coordinate a team of three project staff who will be working at a variety of locations in the city such as waste dump sites, schools, NGOs and others areas. They will be investigating the subject and documenting different cultural, religious and sexual understanding and reactions to it. The Nairobi Team at the Kenyatta University is headed by Dr. Mbugua wa Mungai, the Head of the Literature Department at that University. The DIRTPOL Project is scheduled to span a period of five years beginning from October 2013 to September 2018. During the period, progress reports and research findings will be disseminated through a conference, a web site, a book and academic papers written by the researchers but coordinated by the Principal Investigator and her team of researchers.&nbsp;</p>
<p style="text-align: justify;">
	Professor Babajide Alo, FAS, used the opportunity of his visit to the University of Sussex for the inauguration of the DIRTPOL Project to explore other areas of collaboration between Sussex and Lagos in areas such as split site and bi-directional Ph.D agreements, staff and student mobility and exchanges between the two universities, enhancing student experience- through the &lsquo;Sussex Plus&rsquo;&nbsp;, Junior staff to be sent to Sussex for Ph.D training under Tetfund, participation in the Junior Research Associate Scheme with bursaries for staff on summer programme&nbsp; and cross visits by Senior management, joint bids/applications for relevant international grants and fellowships, recognition of UNILAG foundation programme results for admission to the University of Sussex, among others. These issues formed the basis of his extensive meetings with members of the University&rsquo;s Senior Management led by Professor Chris Marlin, the Deputy-Vice Chancellor (Research) and Professor&nbsp; Michael Davies, the Pro-Vice Chancellor (International).</p>
<p style="text-align: justify;">
	&nbsp;The Deputy Vice-Chancellor (Academic &amp; Research) also had wide consultations with many professors and potential research collaborators at Sussex. &nbsp;A memorandum of Understanding (MOU) based on these issues is being worked out between Lagos and.</p>
