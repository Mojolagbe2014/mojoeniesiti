<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p>
	Worried by limited employment opportunities for young people in Nigeria,&nbsp;the World Bank&rsquo;s Board of Executive Directors has approved $450 million for two projects.</p>
<p>
	The money is meant to boost employment opportunities for young people and to improve education quality for millions of school children. No fewer than 20 states are slated to benefit.</p>
<p>
	&nbsp;An elated World Bank Country Director for Nigeria, Marie-Francoise Marie-Nelly, had this to say about the credit: &ldquo;Investing in people is an essential part of Nigeria&rsquo;s strategy to reduce poverty and achieve steady economic growth.</p>
<p>
	&ldquo;I am delighted that we are supporting better schooling and earning prospects for millions of children and young people, while also cushioning some of the poorest families through a stronger social safety net system.&rdquo;</p>
<p>
	According to Bamidele&nbsp; Oladokun, Communication Associate, Africa Region of the Bank: &ldquo;The $300 million Nigeria Youth Employment and Social Support Operation focuses on poor youth and the poorest 10 per cent of households in participating states.</p>
<p>
	&ldquo;It provides youth aged 18 to 35 with opportunities for work and skills training, and improved access to social services for the vulnerable.</p>
<p>
	The $150 million Nigeria State Education Program Investment Project will serve millions of children in the three Nigerian states of Anambra, Bauchi and Ekiti by improving the quality of their schooling.</p>
<p>
	&ldquo;The project links financing to results achieved through better teacher deployment and school management. It aims to improve measurement of student learning in government primary and secondary schools in these states.</p>
<p>
	&rdquo;The Credits will be financed by the World Bank&rsquo;s International Development Association and is aimed at supporting Nigeria&rsquo;s socio-economic development in line with the Federal Government&rsquo;s Transformation Agenda.</p>
