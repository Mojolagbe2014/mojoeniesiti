<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="text-align: justify;">
	The National Commission for Colleges of Education NCCE has commended the Lagos State Government and management of Michael Otedola College of Primary Education, Noforija, Epe for monumental infrastructural development in the college. The Acting Director, Department of Academic Programmes, NCCE, Abuja, Dr. Muhammed Sanni Aliyu disclosed this when he led NCCE team to the accreditation of 23 courses in college recently. He said that the state government has shown enough interest in the funding of the college and other educational institutions in the state. According to him, what is found in the college in term infrastructure and facilities may not be found in many State Universities.</p>
<p style="text-align: justify;">
	The leader of the team also commended the college management for prudent management of funds released to them, saying that if funds were not judiciously managed, the state government will not continue to give enough to run the college. Inaugurating the team of panelist before embarking on the accreditation exercise Dr. Muhammed charged them to discharge their duties without fear of favor, stressing that the whole exercise was for the college to know its area of strengths and weakness. He implored the team to follow the philosophy and objectives of NCCE as contained in the commission standard book.</p>
<p style="text-align: justify;">
	He went further that relevant data must be collected from lecturers, students and current state of facilities and must also be carefully documented in the reports so that the final reports will assist the regulatory agency in the accreditation exercise. He said that the NCCE Accreditation Team discovered that the strengths of the college outweighed the weaknesses. The team, he stated also observed that the academic works, quality of teaching and learning are impressive. According to the team leader, Lagos State Government and the Governing Council of college have done well in the provision of infrastructures and facilities such as lecture halls, lecture theaters, laboratories, e-library, ICT centre and others for the college.</p>
