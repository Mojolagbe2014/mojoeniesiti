<td style="color:#333; text-align:justify; line-height:20px"><div class="description" style="font-size:13px;"><p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	The Association of National Accountants of Nigeria (ANAN) and the Institute of Chartered Accountants of Nigeria (ICAN) have unfolded collaborative plans to contribute meaningfully in the development of the country. The President of ANAN, Hajia Maryam Ladi IbrahIm noted that the two bodies must &ldquo;begin to think of a joint advisory role to government.&rdquo;</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	The President of ANAN, Hajia Maryam Ladi IbrahIm made the disclosure on Wednesday April 9th when she received the President of ICAN, Adedoyin Owolabi and the Chairman of ICAN Lagos Mainland District Society, Chief Raphael Anyama at the association&rsquo;s Secretariat in Lagos.</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	She said the association is looking forward to collaborating with ICAN on several areas in the interest of the economy adding that &ldquo;it was not proper for issues to come up and both associations would give different versions to government.&rdquo;</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	The ANAN President explained that both bodies had a long struggle to get to where they were today and by virtue of the profession, their members should be holding forth as Minister of Finance, adding that without collaboration, the associations would not get there. Ibrahim said further that &ldquo;If there is an internal arrangement that we want to use to correct the ills of the society, we start thinking that way by collaboration,&rdquo;</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	Ibrahim noted that accountants were constantly being accused of all the rots in both the private and public sectors, but added: &ldquo;If you know any accountant perpetrating such, report to either of the professional organisations so that the person can be disciplined. According to her &ldquo;In ANAN, we are taking it very serious, because it is the only way we can show we are a serious association. Every week, government releases billions of Naira for contracts, ANAN and ICAN should be able to do the right thing by advising government.&rdquo;</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	She opinned further that budget implementation and monitoring should be a joint effort and on national issues, &ldquo;we should be able to have a common ground.&rdquo; She proposed a joint workshop which will be meaningful to government and Nigerians.</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	In his reaction, the ICAN President said he was particularly happy that the relationship between the two bodies had metamorphosed into a purposeful and better image. According to him, &ldquo;Initially, the International Federation of Accountants said we should sponsor and mentor our sister accountancy body. I am happy that we have proved to the whole world that if we must move forward positively, then, we must work together. This is a day in which I have fulfilled one of the things I love doing most &ndash; building bridges.</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	The ICAN president went further to assure ANAN that &ldquo;whatever we can do to assist your association in the future will be promptly done. He stated that at the international arena and locally, ICAN shall be cooperating with ANAN. According to him, both bodies should see each other as active participants in the Financial Reporting Council of Nigeria,&rdquo; .</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	He congratulated Ibrahim for her recent appointment as the Chairman of the Financial Reporting Council of Nigeria, describing the appointment as another feather to her cap which she actually earned. He said said both bodies shall continue to work together, particularly in technical areas of the accountancy profession.</p>
<p style="margin: 15px 0px; padding: 0px; border: 0px; outline: 0px; color: rgb(0, 0, 0); font-family: Arial, sans-serif; line-height: 18px;">
	Concluding, the ICAN president said on budget implementation and planning, the institute was ready to team up with ANAN to do the same thing ICAN had been doing for government.</p>
