	Of the four-point agenda stressed by the Governor, Central Bank of Nigeria, Mallam Sanusi Lamido Sanusi, on assumption of office, the need to strengthen regulation and supervision through enhanced disclosures by financial institutions, stands out clearly. This was sequel to the backdrop of the CBN&rsquo;s regulatory experience as well as the current global financial crisis, which impaired public confidence in the nation&rsquo;s financial markets. Below is an attempt to address some of the questions that have been raised by commentators and operators on the measures taken so far by the CBN to restore confidence to the banking sector.</p>
<p style="text-align: justify;">
	1. What do full disclosure requirements mean? Full disclosure requirements are the mandatory financial, operational and management information, which financial institutions are required to disclose in the rendition of their periodic returns to the regulatory authorities and the public.</p>
<p style="text-align: justify;">
	The process has to do with ensuring the integrity of data in the rendition of reports to the supervisory authority and the public in order to enable them ascertain the true financial position and performance of deposit money banks.</p>
<p style="text-align: justify;">
	2. What are the objectives of introducing the full disclosure requirements? The objectives of introducing the full disclosure requirements are essentially to provide economic agents (depositors and investors) and other stakeholders with appropriate information to assist them in evaluating the financial positions and performances of banks and to enable them obtain better understanding of unique characteristics of the operations of banks. In view of this, an over-arching and implicit objective of banking regulation and supervision is to ensure the emergence of a strong, safe, stable and reliable banking system that will engender and sustain the confidence of depositors and other stakeholders.</p>
<p style="text-align: justify;">
	3. What are the benefits of the measures to the financial institutions and the market in general? The provision of adequate information enhances the integrity of banks and reduces the reputational risks that could lead to loss of confidence and patronage. It will also help to reduce market uncertainty and limit the risk of unwarranted contagion. The effort of the Bank at enhancing the current regime of information disclosure by banks will help to further promote market discipline. When these are achieved, the banks would have been strengthened to play their intermediation role in the economy. The market would also have been better placed to effectively act as a transmission mechanism for monetary policy measures.</p>
<p style="text-align: justify;">
	4. What are the implications of implementing the requirements to the banks? Obviously the banks will be better off with full compliance and implementation of the additional disclosures as directed by the CBN since the merits of full disclosures far outweigh the demerits. The banking sector, like every other segments of the financial system thrives on trust and confidence. The full disclosure option will assist all stakeholders to evaluate the true and fair positions of the banks and confidently take informed decisions about them. Consequently, the confidence of stakeholders will be a major selling point for the banks. The issue of demarketing among the banks would have been systematically and permanently addressed.</p>
<p style="text-align: justify;">
	5. How will the measure affect depositors and investors in the financial market? The full disclosure measures as directed by the CBN are primarily meant to further entrench sound corporate governance in the financial system where accountability, transparency and prudence are the grand norm. Hence the intention of the CBN is to provide users, including investors and depositors, with requisite information to enable them evaluate the financial position, performance and operations of banks with a view to taking well informed decisions as to where to put their CBN&rsquo;s ultimate aim is to have in place a full disclosure regime that will put the nation&rsquo;s banking industry at par with best practice and international standard.</p>
<p style="text-align: justify;">
	The measure is in tandem with best practice and in pursuant for the attainment of the world acclaimed requirements of BASEL 11and International Financial Reporting Standards (IFRS).</p>
