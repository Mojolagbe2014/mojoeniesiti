	Report Highlights: The GON has announced plans to cut wheat imports by introducing a new policy compelling cassava flour inclusion in wheat flour. Part of their plan is to impose a levy of 15 percent on wheat grain imports, which will increase the effective duty from 5 percent to 20 percent. The government also plans to introduce fiscal incentives to stimulate increased domestic production and processing of cassava. This policy direction could greatly reduce U.S. wheat exports to Nigeria.</p>
<p style="text-align: justify;">
	Executive Summary:</p>
<p style="text-align: justify;">
	President Goodluck Jonathan, in his 2012 Budget Speech to the joint session of the National Assembly on December 13, 2011, announced that the government (GON) will introduce policies to encourage the substitution of high quality cassava flour for wheat flour in bread baking. The GON is determined to commence implementation in March 2012, starting with 10 percent cassava flour inclusion in wheat flour. The inclusion rate is expected to increase steadily to 40 percent by 2015. The National Agency for Food and Drug Administration and Control (NAFDAC) is expected to monitor compliance.</p>
<p style="text-align: justify;">
	In addition, the GON plans to introduce fiscal measures that would encourage Nigerians to patronize made in Nigeria goods to increase income and generate jobs. From July 1, 2012, the GON will implement a 65 percent levy on wheat flour to bring the effective duty to 100 percent, while wheat grain will attract a 15 percent levy which will bring the effective duty to 20 percent. Bakeries that attain 40 percent blending would enjoy a corporate tax incentive of 12 percent rebate. With effect from March 31, 2012, the importation of cassava flour will be prohibited so as to further support the program (at present, Nigeria does not import cassava). Furthermore, all equipment for processing of high quality cassava flour and flour blending will enjoy a duty free regime as incentive to bakers for composite flour utilization. Consultations with the sector to ensure a smooth transition are on-going.</p>
<p style="text-align: justify;">
	In a separate meeting with flour millers and bakers, The Minister of Agriculture Dr. Akinwumi, who is the driving force behind the policy, emphasized that the cassava flour inclusion policy is a serious agenda of the Federal Government and tasked them to comply. He asked flour millers to see compliance with the policy as their contribution to national development. He conducted a tasting session with flour millers and bakers and concluded that the composite flour bread is good, tasty and nourishing. In response, flour millers in the country under the aegis of Flour Millers Association (FMA) of Nigeria assured the Minister of their readiness to comply but requested that the implementation of the policy should be gradual, stressing that doing this would make the target achievable by 2015.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	Despite modest increases in processing facilities and drying operations, Nigeria&rsquo;s capacity to produce industrial-grade cassava flour remains limited and as such the cost of cassava flour is considerably higher than imported wheat. This policy was first attempted in 2002 under President Obasanjo. The program was not sustained because of inadequate domestic capacity to process industry grade cassava flour. The Government was forced to reduce the cassava flour inclusion rate down to 5 percent. The program was abandoned altogether in 2007.</p>
<p style="text-align: justify;">
	Nigeria&rsquo;s Cassava Production</p>
<p style="text-align: justify;">
	Nigeria is reported to be the world&rsquo;s largest producer of cassava with output in 2010/11 estimated at about 35 million tons of tuber. The GON&rsquo;s cassava transformation agenda is aimed at encouraging large-scale commercial farming to boost production from the current 35 tons yearly to 51 million tons by the year 2015. In this regard, the GON has listed cassava as one of the six crops to receive increased attention under the recently launched Agricultural Transformation Action Plan (ATAP). According to Dr. Akinwumi, &ldquo;the objective of the Federal Government&rsquo;s cassava initiative is to increase commercial cassava production, prevent or eradicate the problem of cassava glut through marketing, and to use the crop which the country is known for to boost industrial development.&rdquo; A major constraint to this policy thrust is the absence of large and contiguous lands for commercial agriculture due to Nigeria&rsquo;s complex land tenor system. In the past, investors (both local and foreign) after first acquiring land usage rights from regional governments were made to pay a second time by local level interests.</p>
<p style="text-align: justify;">
	The cassava committee of ATAP has established six sub-committees to focus on clearly identified cassava use value chains. The value chains are for starch, sweeteners, dry chips, ethanol, high quality cassava flour and garri (grated, fried, and dried cassava for direct food use). The key objective is to raise productivity and secure demand for cassava. Membership of the sub-committees include experts from the Universities, NGOs, cassava Growers Association, cassava processors and marketers, banks, industry groups, and the relevant departments of the Federal Ministry of Agriculture.</p>
<p style="text-align: justify;">
	Prospects for the New Initiative</p>
<p style="text-align: justify;">
	Processing capacity has improved somewhat since the first attempt in 2002. The decision of the GON to implement a zero percent import duty on cassava processing facilities will encourage new investments in processing facilities. The Chairman of the cassava transformation committee told FAS Lagos that the GON has ordered 18 large cassava processing factories from China that would later be leased to private sector investors. Each factory is capable of processing 1000 tons of cassava tubers per day. However, investors are wary that the policy may not be sustained, just as in the past, and such investments would become unprofitable. Mr. Ayo Olubori, President, National Association of Cassava Processors and Marketers was recently quoted to have said that &ldquo;the lack of political will to religiously execute policies and other factors such as the negative attitude of flour millers have caused the shutting down of 100 cassava flour plants across the country.&rdquo;</p>
<p style="text-align: justify;">
	Bakers&rsquo; Response</p>
<p style="text-align: justify;">
	In general, bakers and flour millers think that 10 percent cassava in wheat flour is achievable. But beyond that level cassava would introduce odor and aftertaste to their products, reduce shelf life of bread from an average of 7 to 4 days, and drastically alter the baking characteristics. Technically, bakers think that at a 40 percent cassava inclusion rate, bread ceases to be wheat bread because the cassava will predominate in baking characteristics, especially because of its zero protein content. High cassava flour content in bread will require higher doses of bread improvers, especially protein and preservatives.</p>
<p style="text-align: justify;">
	Post&rsquo;s discussions with bakers revealed a somewhat neutral attitude to cassava flour inclusion in wheat flour policy at 10 percent level. The President of the Master Bakers Association of Nigeria said that bakers are ready to use whatever is available as long as it would add value to their business. He advised that &ldquo;the government should implement the proposed 40 percent inclusion of cassava into bread making slowly since the 10 per cent initiative in the past failed because of gap in supply of cassava flour.&rdquo; He added that small-scale bakers would be required to make new investments in spiral mixers as the traditional hand mixing method will not work with cassava flour inclusion. In all, bakers expect the price of bread to go up by about 15 percent and demand to dip as a direct of the higher price and consumer preferences. One 40 percent cassava flour bread recipe calls for the use of twelve eggs per loaf, which if utilized could make the per loaf price prohibitively expensive.</p>
