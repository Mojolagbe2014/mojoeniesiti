<p>
	Introduction: Change hits hard, fast, and often. It shifts our focus, changes our direction and alters our plans. Change leaves us stumped by questions we&rsquo;re not prepared to answer and searching for questions that we never thought to ask. Left on the road, between what we were once sure of and the indecision of which way to go; a problem waits to be solved.</p>
<p>
	Problems begin with one Unanswered Question</p>
<p>
	Hearing the word problem, we automatically think of some catastrophic event requiring kick-off meetings, project teams, and an all-out hunt for the illustrious root cause. Usually, however, problems are much more subtle than that. They move in quietly, riding the coat-tails of change or they drag change along, bringing it to our doorsteps. Problems both follow and precede change. Most problems don&rsquo;t need a grand introduction. All we need to do is to look for them, wait for them, and prepare for them. They are always there, just beneath the surface. And before they ever took a life of their own, even those problems with the deepest roots started simply enough as an unanswered question. What issues currently have your organization tied up in knots? What was the last problem that you attempted to solve? What was the last problem that you ignored? This manifesto is intended to dispel the myth that problems need official-sounding names and formally outfitted team leaders wearing colored belts. Problems are not only exposed through formal processes but are revealed in a moment of curiosity. Just around the corner of expectation and at the intersection of &ldquo;why; why not; and if not me, who?&rdquo; is a chance for every employee to positively influence the course of events.</p>
<p>
	The following is a problem in the making:</p>
<p>
	&nbsp;Friday morning a shipment of boxes was delivered to a distribution warehouse in a small North Carolina town. As had happened on many Friday mornings before, Jason Checkins received the shipment and pointed to the area where the pallets should be placed. As the boxes were stacked, Jason noticed that the boxes all had yellow stick-ers. He thought that it was odd and wondered to himself, &ldquo;Why don&rsquo;t these boxes have the blue labels that they normally do?&rdquo; He thought about it for a moment and moved on. He never mentioned the yellow labels to anyone in the facility until the following Friday, a week later.</p>
<p>
	ParT 1:</p>
<p>
	What Happens to a Problem Deferred?</p>
<p>
	Problems often come first in unseen whispers. They are more than headaches to avoid; they are signals of things to come&mdash;flashes of lights drawing us to attention and calling us to ac-tion. Before we can resolve them, we have to increase our ability to predict them, sense them, see them, and examine them. When I think of organizational problem solving, it brings to mind a poem written by Langston Hughes, &ldquo;What Happens to a Dream Deferred?&rdquo;. We sense that something may be wrong, but we stand back. We watch and we wait to see what is going to happen. We watch the market; we see the effects on our competitors and our suppliers. We watch what&rsquo;s happening around us, to our employees, and to our co-workers. We read the headlines and hear the news of industry fallout and thousands of jobs being lost. Still we fail to consider what those signs might mean for us. Only rarely do we look for opportunities to make a difference. Pointing his pen at the corporation, today Mr. Hughes might ask: What happens to a problem deferred? Does it dry up like a raisin in the sun? Or fester like a sore&mdash; And then run? Does it stink like rotten meat? Or crust and sugar over&mdash; like a syrupy sweet? Maybe it just sags like a heavy load. Or does it explode?</p>
<p>
	Problems don&rsquo;t Just go away. You don&rsquo;t have six months to form an assessment commit-tee or three months to train all your people. You can&rsquo;t afford to lose time pretending that the problem does not exist, or even one day wondering why someone else has not taken action. It is the job of every person in the company to do what he can, when he can&mdash;and hopefully before it is too late....</p>
