	For the past five years, I&rsquo;ve been working hard in preparation for writing another book. As part of my research, I&rsquo;ve read literally hundreds of books, articles, and research papers on the key strategies of the world&rsquo;s top companies&mdash;many of which, I am honored to say, are current clients of mine. I&rsquo;ve done all this work to see if I could uncover a pattern of business excellence. At last count, I have read and analyzed about 160,000 pages on best practices and winning strategies, and condensed this information to a single page of bullet points. Yes, that&rsquo;s right&mdash;one single page! Combined with my 14 years of hands-on experience working directly with senior leaders at more than 20 Fortune 500 firms and a number of America&rsquo;s top private companies, I think I am getting a pretty clear picture of what all of these organizations focus on in their efforts to achieve and sustain highly successful businesses. I&rsquo;d now like to share those ideas with you in the hope that you can take this information and apply it to your organization, employees and customers. Before I get into the pattern, though, we first need to set the stage with a few key elements that form the foundation of any successful business. The first is that, at the very least, you must produce a high-quality product or service. If what you sell is not worth buying, no amount of good ideas, cool strategies, or slick marketing will help you. All sustainable business success is built on delivering real value to the customer&mdash;period. The next given is that you need to have a solid handle on your financials. I love the old saying that if you aren&rsquo;t managing cash flow, you won&rsquo;t be managing much for long! Even great companies&mdash;companies with amazing products, outrageously good services, loyal customers, and fantastic strategies&mdash;have been reduced to ruin and driven to bankruptcy by poor financial management.</p>
<p style="text-align: justify;">
	All sustainable business success is built on delivering real value to the customer&mdash;period.</p>
<p style="text-align: justify;">
	The last given is that change is inevitable. There is no single strategy that will carry your company forever&mdash;just ask my buddy Tom Peters, who wrote the fantastic book In Search of Excellence back in 1982, only to watch more than half of the companies he highlighted go out of business! Markets shift, consumer preferences change, new competitors appear, technology advances&mdash;and so must you. Even though I can recommend which of today&rsquo;s popular strategies I believe deserve your attention, there is no guarantee that these same strategies will still be as relevant in 20 years. I think they will, but no one can see that far ahead. With all of that said, following are the six strategies on which all the great companies I studied were relentlessly focused.</p>
<p style="text-align: justify;">
	1. vivid vision:</p>
<p style="text-align: justify;">
	A clear and well-thought-out vision of what you are trying to create that is exceptionally well communicated to everyone involved. I have had many leaders tell me that they don&rsquo;t believe in &ldquo;the vision thing,&rdquo; or that they don&rsquo;t have time to work on something so vague. Folks, nothing could be further from the truth! A vision is not some meaningless schlock that you throw together on a two-day retreat and post all over the office, never to look at again. A true vision is an exciting, focused, realistic, and inspiring picture of what you and your people are all trying to accomplish together&mdash;it&rsquo;s the reason you come to work every day, the impact you want to make on the world, the kind of company and product you aspire to build. Your vision does not have to be a Pulitzer Prize-winning masterpiece of literature; it simply needs to be something that everyone can clearly understand and about which people are damn excited. When I put it that way, believe me&mdash;you don&rsquo;t have time NOT to sit down and create a vision for your business. In addition, I do not ascribe to the camp that says a vision absolutely must be specific, detailed and measurable. I love visions that are&mdash;they work very well, and they give people a great idea of where they are going, which is extremely valuable. But I have also seen some really successful businesses&mdash; often in high technology, where the velocity of change is overwhelming&mdash;that have visions that are more about how they do business, and what is most important to drive success, than they are about revenues or market share. I have one client whose vision is focused on building a superior TEAM that delivers real value through elegant SOLUTIONS for their customers and looks for appropriate GROWTH opportunities. These three ideas&mdash;team, solutions and growth&mdash;have taken this firm from $50 million to $250 million in just a few years. That sounds like a pretty successful vision to me!</p>
<p style="text-align: justify;">
	One last key idea on the vision: It must be communicated relentlessly to all stakeholders. Through emails, meetings, speeches, conference calls, corporate retreats, and other means, the vision needs to be repeated over and over again. Once a CEO asked me, &ldquo;When do you know that you have talked about the vision enough?&rdquo; I replied, &ldquo;When you talk about it so much that you feel like you are going to be nauseous, that is when the lowest person in your company just heard it for the very first time!&rdquo; The goal is to send a clear and consistent message about where you are going, why it is important, and how you will get there.</p>
<p style="text-align: justify;">
