      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	Report Highlights: Although Nigeria dropped from the number one export destination in the world for U.S. wheat in MY2010/11 to third position this year, it remains the most consistent and loyal customer. Sales as of April1 are a record 3.5 million tons, up from 3.3 million tons last year. Consumption continues to expand due to increased demand for wheat flour for bread, noodles, pasta and biscuit production. Nigeria&rsquo;s rice imports from the U.S. are also trending upwards, largely based on quality. Local grain production is reported to be increasing. The increase is attributed to the steady availability of inputs and favorable weather conditions. Grain production in MY2011 is expected to increase due to the reported early arrival of rains in the grains&rsquo; belt and the stimulus of the prevailing attractive grower prices.</p>
<p style="text-align: justify;">
	Executive Summary:</p>
<p style="text-align: justify;">
	Since the lifting of the wheat ban in 1992, imports have been increasing steadily due primarily to increased consumption of bread, biscuits, noodles and pasta. Per capita wheat consumption has more than tripled from 6 kg to 20 kg during the same period. Nigeria is a huge growth market for wheat, with U.S. sales this year at a record level of 3.5 million tons. The combination of a steady increase in domestic demand for flour based products and high prices for local substitutes is encouraging millers to bring more of the existing excess milling capacity into use. U.S. market share remains dominant at almost 90 percent. Competitors are Argentina, Canada and the EU.</p>
<p style="text-align: justify;">
	Domestic rice production continues to increase due largely to incentives available to farmers under the Presidential Initiative on Rice. This initiative is part of the Government of Nigeria&rsquo;s (GON) efforts aimed at achieving self&ndash;sufficiency in rice production. Nevertheless, imports are expected to be steady and Nigeria will remain one of the world&rsquo;s largest rice importers. At present importers who established milling facilities in the country are taking advantage of the GON&rsquo;s lower duty on brown rice to import the product. Despite renewed efforts by the GON to curb smuggling, a substantial portion of Nigeria&rsquo;s rice imports continue to enter the country through the porous borders, especially from the Republic of Benin. Rice smuggling is fueled by the GON&rsquo;s high import tariffs.</p>
<p style="text-align: justify;">
	Nigeria&rsquo;s aggregate grain production in 2011/12 is forecast to increase five percent. The forecast is based on a number of factors including expanded planted area as a result of the prevailing attractive grain prices, the GON&rsquo;s zero tariffs on imported agrochemicals, and the timely arrival of rains in the grains belt. It is election year in Nigeria and governments at local, state and federal levels have promised to provide fertilizers to farmers at a subsidized rate. Although fertilizer trade is deregulated, federal, state and local governments are involved in its distribution, applying differing levels of subsidies. In 2010, the subsidy was 25 percent at the federal level, 10-30 percent at the state level, and 10-15 percent at the local government level. Despite this multi-layer subsidy program, benefits rarely the intended farmers because of political interference in the distribution channels. The average street price of a 50-kilogram bag of fertilizer is 5,500 naira ($37), compared to 2,000 Naira ($13) for subsidized product. Fertilizer and agricultural tools/implements, which are essential for agricultural development, have zero import duty.</p>
<p style="text-align: justify;">
	Despite the good corn crop in 2010/11, prices remain high due largely to the low carry over stock from the preceding year, rising domestic demand, and strong demand from the poultry and brewing sectors. Market sources indicate that prices will continue to rise until new supplies are available from new crop starting in August 2011. In September 2008, the GON lifted the import ban on corn and local poultry producers are exploring import opportunities to cushion the impact of high prices. Corn is the preferred energy source and accounts for about 60 percent of compound feed. Production of corn and sorghum is expected to increase in 2011/12 due to attractive producer prices this year and greater availability of inputs. Nigeria and the United States are the two largest producers of sorghum in the world.</p>
<p style="text-align: justify;">
	The overall policy thrust of the GON is self-sufficiency in food production. Government policy is focused on three key objectives: provision of production inputs, such as improved seeds and agro-chemicals; storage of at least 10 percent of grains production in the Strategic Grain Reserve; and the development of irrigation facilities to enhance all-year round cropping and value addition.</p>
<p style="text-align: justify;">
	Exchange Rate: US$1 = 150 Naira</p>
<p style="text-align: justify;">
	Commodities:</p>
<p style="text-align: justify;">
	Wheat</p>
<p style="text-align: justify;">
	Production:</p>
<p style="text-align: justify;">
	Nigeria&rsquo;s wheat production in 2011/12 is forecast to remain small at only 100,000 tons, the same as in 2010/11. Local climatic conditions in Nigeria are not suitable for profitable wheat production and the wheat that is produced is grown under irrigation in a few states in northern Nigeria.</p>
<p style="text-align: justify;">
	Consumption:</p>
<p style="text-align: justify;">
	Nigeria&rsquo;s overall milling capacity is currently estimated at about 6.5 million tons, up from 6.0 million tons. Capacity utilization is estimated at about 60 percent in 2010/11. The additional investment in capacity is an indication of investor confidence in the future growth of the milling industry. Flour Mills of Nigeria continues to be the market leader by capacity but new entrants into the market, such as Dangote, Honeywell, and BUA, continue to increase market share. The entrance of these new and aggressive millers into the Nigerian flour milling industry has increased competition based on price and quality. Additionally, the Nigerian baking industry continues to expand and upgrade its production facilities. There is a proliferation of small and large independent bakeries and retail in-store bakeries. Increased competition has resulted in an increase in the variety and quality of fresh baked products available to consumers.</p>
<p style="text-align: justify;">
	Consumption patterns are changing in tandem with growth of the middle class. Production of bread flour continues to expand because bread is a standard item in the breakfast diet and it is a convenience food for many Nigerians. The rapid grow in the quick service restaurant industry offering pastries in recent years have also contributed to the increase in wheat demand. At present, Nigeria is experiencing the greatest growth in the production of noodles as virtually all flour mills in the country have established noodle production facilities. The demand for noodles in Nigeria is very high and noodle imports are banned. Nigeria&rsquo;s noodle manufacturers have benefited from the removal of the ban on crude vegetable oil, a key component in instant noodle production and increased imports of palm oil has resulted in a drop in the cost of production. Noodle production is estimated to use up to 500,000 MT of Hard Red Winter Wheat (HRW) in 2010/11. Although Nigeria is traditionally a market for Hard Red Winter, in recent years there has been a steady increase in demand for other types of wheat such as Soft Red Winter for use in biscuit production, Hard White Wheat for bread and noodle production, and durum for pasta.</p>
<p style="text-align: justify;">
	Nigeria is a price sensitive market. The price of wheat in the international market increased considerably in 2010/11 due to tight global supplies. Millers have not passed onto consumers the full price increase because of market resistance.</p>
<p style="text-align: justify;">
	According to industry sources, informal wheat flour exports to the neighboring countries is estimated at approximately 400,000 metric tons (570,000 MT of wheat), representing 15 percent of total flour production in Nigeria. Branded Nigerian flour can be found in several countries in West and Central&hellip;.</p>
