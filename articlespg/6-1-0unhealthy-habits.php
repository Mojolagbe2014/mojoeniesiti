      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	After years of working with entrepreneurs, investors, corporations, and institutions, I realized that most people have one or more habits that prevent them from being wealthy. For each habit I have created and tested a counter-habit, hoping that this can help individuals live happier. This is a manifesto for individual people, written to address one person at a time.</p>
<p style="text-align: justify;">
	Why do I call them habits? Habits are transparent to us. Usually we don&rsquo;t perceive them unless someone points them out, so I want to create that sense of awareness on the habits that I&rsquo;ve found have the bigger impact. Before I continue, a word of caution.</p>
<p style="text-align: justify;">
	Wealth does not make you happy, but when you are wealthy, you are happier. Why? Because you feel valued, important, respected, appreciated&hellip; but most importantly, because if you are wealthy you can do things freely. Of course, you can feel valued without feeling wealthy, and you can feel happy without feeling wealthy. But if you take care of your wealth, you stop worrying about it. You feel in control. There is ample information about how to make money, how to be wealthy, how to be successful, how to.... Yet, here we will take an opposite look. What about looking at things you are doing that are preventing you from being wealthy? So I first looked at my personal experience, how I moved from being very poor, to being almost broke, to managing an investment fund, to retiring before I was 45. I also asked tons of questions and analyzed what others were saying and thought. Finally, I broke all of this information into the 10 key habits that unwealthy people do. I am certain that by first observing and secondly taking action to change those habits, your wealth will take a huge turn. You will read about 10 people acting out the habits and their reasoning. I find that by observing others, we learn a lot about ourselves. I end each habit with a vaccine, an antidote. It is not by doing the opposite but by injecting the vaccine into your life that you become better at managing each habit. At the end of the manifesto I&rsquo;ve also included my personal system to replace old habits.</p>
<p style="text-align: justify;">
	1. Feeling Sorry for Yourself</p>
<p style="text-align: justify;">
	Unwealthy people think that life is unfair and that being wealthy is not an option for them.</p>
<p style="text-align: justify;">
	&bull; Anna feels sorry because she is a woman, men have better options.</p>
<p style="text-align: justify;">
	&bull; Peter feels sorry because he is black, whites don&rsquo;t have to face racism.</p>
<p style="text-align: justify;">
	&bull; Sophia feels sorry because she is fat, skinny people get better jobs.</p>
<p style="text-align: justify;">
	&bull; John feels sorry because he is short, taller people have a more impressive appeal.</p>
<p style="text-align: justify;">
	&bull; Carolyn feels sorry because she is too skinny, other people look healthier.</p>
<p style="text-align: justify;">
	&bull; Mark feels sorry because he is single, married people have love to go home to.</p>
<p style="text-align: justify;">
	&bull; Joan feels sorry because she is married, single people have more fun.</p>
<p style="text-align: justify;">
	&bull; Tom feels sorry because he is divorced, married people have their kids around.</p>
<p style="text-align: justify;">
	&bull; Lisa feels sorry because she is young, older people have more respect.</p>
<p style="text-align: justify;">
	&bull; Paul feels sorry because he is old, younger people have more options.</p>
<p style="text-align: justify;">
	So.. if you feel sorry for yourself, how would you expect others to feel about you?</p>
<p style="text-align: justify;">
	Unwealthy people focus on what is wrong with their lives&hellip;</p>
<p style="text-align: justify;">
	and focus on it&hellip;</p>
<p style="text-align: justify;">
	and focus on it&hellip;</p>
<p style="text-align: justify;">
	Feeling sorry for yourself is like an anchor, preventing you from living fully, from having joy, happiness, and fun, from supporting others, from requesting help, love, commitment. Feeling sorry for yourself is a great way to sell yourself short and be busy finding all the things you are missing in life. Feeling sorry for yourself diminishes the value you expect, create, and demand from others. You cannot be wealthy if you are feeling sorry for yourself. And believe me, you will find enough reasons to convince yourself of what a great loser you are! The vaccine to feeling sorry? Being grateful! Focusing on what is going well in your life creates a circle of pride and healthy selfishness and sets you up in a much powerful platform to give your best and expect the best. Then you feel valuable, and it shows. You work on your strengths, on your passions, on your best you. You focus on making the best of your life and that includes taking responsibility for your resources.</p>
<p style="text-align: justify;">
	2. Being Greedy</p>
<p style="text-align: justify;">
	Unwealthy people think that in order to be wealthy you need to keep as much as you can and get the most of your money. They love &ldquo;great values&rdquo; and seek opportunities to make the most of whatever resources they have&hellip;.</p>
<p style="text-align: justify;">
