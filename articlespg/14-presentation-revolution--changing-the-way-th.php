      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	I&rsquo;m dissatisfied. Even more so&hellip; I am disturbed, and you should be as well. Today we live in a business culture that abuses the art and science of public speaking everyday. We power up our PCs (Macs, if we&rsquo;re lucky) and present poorly prepared and hideously designed presentations to audiences who want to be inspired but never get fulfilled. It is a business climate where PowerPoint is the norm and reading from your slides is standard.</p>
<p style="text-align: justify;">
	It is a business climate where aimlessly pulling an audience is regular. It is a business climate where wasting an individual&rsquo;s time is a given. It is a business climate that is losing out on inspiring and motivating others to make a difference or even change the world. Every man and woman has the opportunity to make a difference when public speaking. After all, what other activity in life allows you the chance to stand in front of a group, crowd, or stadium full of anxious listeners&mdash;watching, waiting, ready to hear you? It can be a very empowering experience. Take advantage of it. Live it. Breathe it.</p>
<p style="text-align: justify;">
	Change the world one presentation at a time.</p>
<p style="text-align: justify;">
	Every person has a dream&mdash;to make a difference&mdash;to make an impact&mdash;to change the world. Unlock the power in you. Only then will you be able to grow into everything you are capable of becoming&mdash; a person who is extraordinary. Only a few have tapped into their extraordinary abilities. They are the thinkers, the revolutionaries, and the nonconformists who have come and gone before us. These are the men and women who crafted speeches that made every man, woman, and child dream great dreams and believe that mankind is capable of almost anything. Each one of us is a powerful creation&mdash;a source of light, hope, love, and conviction that can contribute beyond anything imaginable. The question is: where is that contribution? Who is changing the world today? Why isn&rsquo;t it you?</p>
<p style="text-align: justify;">
	It&rsquo;s time to start a presentation revolution. It&rsquo;s time to be a rebel.</p>
<p style="text-align: justify;">
	According to the dictionary, &ldquo;to rebel&rdquo; is defined as: to resist or rise against some &nbsp;authority, control, or tradition. If a businessperson wants to create a legacy and succeed at giving a presentation, he or she needs to rebel against all three. Here&rsquo;s how:</p>
<p style="text-align: justify;">
	Authority: Let&rsquo;s face it&hellip; most presentations that are given in the corporate environment are given by those in authority. This includes managers, executives, and board members. Maybe you are one of them. Maybe you are not. How many times have you been in a presentation where an executive or your boss has stepped up to the podium, aimless and almost insecure, with nothing good to say? It doesn&rsquo;t have to be this way.</p>
<p style="text-align: justify;">
	Control: A great presenter knows how to take command. A terrible presenter knows how to control. There is a significant difference between command and control. Command is about guiding and directing. It is providing an environment where the audience has faith and trust in knowing where you are taking them. Control is forceful. It&rsquo;s pushy and it turns people away. Fight the evils of control.</p>
<p style="text-align: justify;">
	Tradition: Don&rsquo;t you love those corporate templates? Nobody likes them, yet all companies seem to use them. Break out of the traditional and try something new. Perhaps you should use large visuals and minimal text. Maybe even no visuals and just a dynamic performance. Your audience will appreciate it. I promise.</p>
<p style="text-align: justify;">
	A presentation rebel should really be defined as someone who:</p>
<p style="text-align: justify;">
	&ldquo;Revolts against the ordinary and fights for change. A rebel engages with the people and world around them through tireless contribution. He or she bestows their knowledge and shares their gifts with others, which in turn equips their listeners with the tools, knowledge, and resources needed to help them grow and become extraordinary. And most importantly, a rebel lives life to the fullest. In the words of a wise man, they understand that life is not a &ldquo;got to&rdquo; moment, but a &ldquo;get to&rdquo; moment.&rdquo; You need to be a maverick when it comes to presentations. You need to fight against authoritarian egos or maybe even yourself. You need to command, not control, and you need to break tradition. By doing these things, you have become a rebel with a cause&mdash;a good cause. Every revolution and cause worth fighting for contains deep roots&mdash;a purpose and mission. Rebellion is about creating change&mdash;sometimes to break new ground, and at other times, to get back to the starting point. In this case, it&rsquo;s the latter. The ancient Greek philosopher, Aristotle, taught about the essentialness of three presentation components. They are ethos, pathos, and logos.</p>
<p style="text-align: justify;">
	Ethos&mdash;The ability of the presenter to establish credibility with the audience.</p>
<p style="text-align: justify;">
	Pathos&mdash;The ability of the presenter to display and arouse passion with the audience.</p>
<p style="text-align: justify;">
	Logos&mdash;The ability of the presenter to exemplify the stature of an expert with the audience.</p>
<p style="text-align: justify;">
	All three concepts require grit, determination and hard work. They are about building trust (ethos), winning people (pathos), and delivering results (logos). What rebel behaviors can turn our world around? In modern terms, these basic components can be accomplished by a rebellious approach to the content, design, and delivery of a presentation. If you are locked into presentation conformity, presentation rebellion is the key&hellip;.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	Scott B.Schwertly</p>
