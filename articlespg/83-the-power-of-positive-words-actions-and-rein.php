      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;">The general definition of the term <strong>WORD</strong> is to say something, name, express, command, order or instruct.&nbsp; &nbsp;<strong>ACTIONS</strong> simply put is how we act, react, operate or the effort we put in to do something.&nbsp; <strong>REINFORCEMENT</strong> could be summarised as what helps to build up or increase anything.<br />
How do we foster children&rsquo;s holistic development with words, actions and reinforcement?&nbsp; I would like you to pause for a minute to reflect on how you have been doing this.&nbsp; I have come across practitioners and parents who find this very challenging and have used it positively or negatively towards a child which in turn has resulted in either supporting or destroying a child&rsquo;s confidence.&nbsp;<br />
I will be paying attention to the use of words, actions and reinforcement in the social, personal and emotional aspects of a child&rsquo;s development.&nbsp; I believe that once a child has confidence in these areas, it is then easy for a child to overcome challenging behaviours and other types of fears.&nbsp; Working with children over the past eight years led me to observe and research skills on how to support children with low or no confidence in all areas of learning. &nbsp;I have also had the opportunity to discuss with colleagues, professionals, parents and carers about the possibility of using appropriate words, actions and positive reinforcement to encourage children in all aspects of learning, especially in the areas of social, personal and emotional development.
<ul>
	<li>
		These are some of the skills I have used with children and I did achieve positive results in majority of the cases:</li>
	<li>
		Encourage the child to give you eye-contact and re-assure the child with words and facial expressions, by letting them know that it is okay to express no understanding of a subject matter.</li>
	<li>
		Ask the child to explain what he/she did not understand by using the question - What part of the topic did you not understand?&nbsp; You could then further give specific examples like, Was it the part where the man said a word to the boy?</li>
	<li>
		Explain to the child that he/she can write what they think of the subject and encourage them by using words like, I know you can try, I know you are able to do it.</li>
	<li>
		To make it fun, sometimes, you can become a child and also do the work alongside the child.&nbsp; This I found encouraged a lot of children.</li>
	<li>
		Set realistic goals for the child according to their ability at the time.&nbsp; Let your instructions be clear and simple.&nbsp; Do not group or compare the child with others who are way ahead.&nbsp; Individual planning is important to support children with little or no confidence.</li>
	<li>
		Be patient and be very understanding. &nbsp;Avoid showing annoying, irritating or impatient body language.&nbsp; For little efforts achieved by the child use words like, Well done! You see you can do it if you try.</li>
	<li>
		Parents, carers, teachers and everyone involved in the child&rsquo;s life should work together using same agreed skills to avoid conflict and confusion.&nbsp; &nbsp;Use lots of praise language.</li>
	<li>
		Allow the child to assess him or herself by comparing the progress in their work. For example, if the child has written two sentences today and one the day before, show the child and let him or her see that he or she has made good progress.&nbsp; Children take pride in this and will most likely be willing to go a step further the next day.</li>
	<li>
		Praise the child&rsquo;s efforts in the presence of his/her classmates and your colleagues.</li>
	<li>
		Encourage the child to show his or her work to another colleague or senior head of school.&nbsp; These are incentives that will motivate the child to work harder.&nbsp;</li>
	<li>
		Encourage the child to answer questions in class or in a group by calling on the child to give his or her views.&nbsp; Parents/carers do the same at home.&nbsp; Involve the child in some of your discussions and ask for their opinion.&nbsp; Try to take their opinion on board and let them see that you appreciated their opinion. This is a big boost for their confidence. &nbsp;Even if the answer is not in line with what you expect, first acknowledge the child&rsquo;s comments or views and then give the child a scaffolding clue.</li>
</ul>
For children who have challenging behaviour, I have used the following strategies:
<ul>
	<li>
		Observe what triggers the behaviour and how long that behaviour usually lasts.</li>
	<li>
		Observe what triggers the child&rsquo;s happiness and how long they stay happy.</li>
	<li>
		Ignore the repeated challenging behaviour and draw attention to the positive behaviour around them.</li>
	<li>
		Avoid labelling a child with a negative word.</li>
	<li>
		Avoid using commanding tones.&nbsp; Instead, be assertive using firm facial expressions.</li>
	<li>
		Avoid nagging the child and concentrate on the little positive things that the child does.&nbsp; This way, the seemingly attention seeking behaviour is gradually eradicated.</li>
	<li>
		Put the child in charge of that behaviour so that they take responsibility.</li>
	<li>
		Explain to the other children and adults that they have a role to play because they need to help the concerned child stop the unacceptable behavior.</li>
	<li>
		Where possible, act out that behaviour with someone else. Then ask the concerned child to tell you what he or she felt about the actions they just saw.</li>
	<li>
		Praise the concerned child for not showing the challenging behaviour before it is expressed.</li>
	<li>
		Don&rsquo;t be quick to react or speak except when it is an obvious health and safety matter.&nbsp; Listen to the child; acknowledge what he or she has to say about what triggered a behavior.&nbsp; Show empathy and understanding.&nbsp; Ask the child to think and give ideas of how they could have handled the matter differently.</li>
</ul>
Finally, I believe that if we try to use more positive words, positive actions and positive reinforcement, it is very possible that society will see more confident and happy children coming out of our schools and homes.</div><br /><br />
