      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	Report Highlights: The Government of Nigeria&rsquo;s (GON) attempt to remove its gasoline subsidy on the first day of year 2012 resulted in an industrial strike, severely crippling economic activity for the week that it lasted. After lengthy negotiations with organized labor, the GON was able to remove 1/3 of the subsidy, taking the pump price from 65 Naira to 97 Naira per liter (about US $2.42 per gallon). Also, the disruptive activities of the &quot;Boko Haram&quot; (BH) Islamic sect in northern Nigeria, where the major part of Nigeria&rsquo;s food supply is produced and consumed, have upset agricultural production and distribution as access to farms and markets has become risky and expensive. These two elements have combined to markedly increase the costs of food and food marketing in Nigeria. While Nigeria has been able to &ldquo;muddle&rdquo; through these two developments so far, sustained problems in food production and distribution pose significant threats to the nation&rsquo;s food security.</p>
<p style="text-align: justify;">
	General Information:</p>
<p style="text-align: justify;">
	The Government of Nigeria (GON) attempted to remove its gasoline subsidy effective on the first of January 2012. In response, organized labor led an industrial strike that severely constrained economic activity for one week. The GON subsidy kept the official gasoline price at 65 Naira per liter. Its removal pushed the price to between 140 and 150 Naira per liter. After lengthy negotiations with organized labor leaders, the GON only reduced the subsidy by 1/3, pushing the pump price to 97 Naira per liter (about US $2.42 per gallon). The GON is anxious to remove the remaining subsidies, as the underlying budgetary pressures remain, and reportedly wish to do so before the beginning of April. Northern Nigeria accounts for a significant amount of the production and consumption of food staples (especial maize, sorghum, millet, rice, tubers, etc.), fruits, and vegetables. Additionally, access to farms across the region has become risky due to heightened security concerns over the disruptive activities of the &quot;Boko Haram&quot; (BH) Islamic sect. For at least the past year the Boko Haram Islamic sect has been reportedly involved in numerous bombings and attacks on government offices, police stations, markets, and military barracks across the northern portion of the country. The higher risk factor has increased the cost of food and transportation. Both of these developments are combining to threaten domestic food supplies and distribution in the country and are not expected to improve as the year progresses.</p>
<p style="text-align: justify;">
	Post visited officials of the traders association at the &quot;Mile 12&quot; fruits and vegetables market to ascertain the impact of BH activities and gasoline subsidy strike on their activities. The &quot;Mile 12&quot; fruit and vegetable market in Lagos, Nigeria, is reported to be the largest single fruit and vegetable market in Africa, with an estimated 300 million Naira ($2 million) sales per day. Over 99 percent of products sold in this market are produced in the northern states where the BH activities are the most concentrated.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	The cost of transporting produce from the northern farms to consolidation points further south has increased by over 200 percent. The jump in costs is due partly to increased security concerns and to the recent fuel subsidy removal. Distributors report that fuel is mostly purchased from informal sources in the farm areas at prices 20-30 percent higher than the new official price. Distributors consolidate loads of agricultural products, then haul them to Lagos in 30 ton trucks. Loads take longer to run the delivery corridor, as numerous security checkpoints require multiple off-loading and reloading of the trucks, adding days onto the schedule. The running costs of these trucks have risen from 120,000 Naira to 300,000 Naira per 30 ton truck per delivered load. According to market officials, supply to the Lagos market has declined about 80 percent, from 200 to 40 trucks per day. And consumer resistance to the price increases has meant a drop in sales of what has been delivered. Much of these products are perishables and cannot be held for more than 4 days before they must be discarded. As a result, both farmers and traders incurred huge losses during the week-long nationwide subsidy strike. Many are still struggling to adapt to their new financial constraints.</p>
<p style="text-align: justify;">
	Whereas the situation has put many out of the food business temporarily, the more optimistic and financially strong players are selling at markedly higher prices. Currently the retail prices of fruits, vegetables, and other perishables have increased 40-60 percent due mainly to the supply fall-off. Average prices of other staple foodstuffs with longer shelf lives have also seen some increases. Compared to the first quarter of 2011, the price of rice (mostly imported) has increased about 10 percent for the same period in 2012, due partly to increase in domestic transportation costs and to the effect of a 6 percent Naira currency devaluation late last year. Prices of staple foodstuffs produced locally such as beans, &quot;garri&quot; (cassava tubers peeled, washed, grated and fried into granules), sorghum, millet, yam, etc. are up by an average of 20 percent in this first quarter of 2012 as compared to the same period in 2011, even though demand has generally dropped about 25 percent within the same period.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	Most of Nigeria&rsquo;s food processing operations, processed food distribution facilities, and importers are located in southern Nigeria. The processors and distributors report that the BH activities have shrunk their northern markets, as distributing to markets in the region has become much more time-consuming and expensive. One Nigerian importer wholesaler reported that since his trucks now take seven days to reach Kano, when it used to be a two day journey, he has ceased serving that market for the time being. As a result, retail prices in the north have risen by 40-60 percent for some processed foods compared to the same period last year. Operating costs for processors and distributors have increased by more than 60 percent and 30 percent respectively. Generally, processors are challenged by the increasing cost of raw materials, shrinking consumer incomes, consumer price resistance, higher security risk, as well as suffering from reduced demand across their markets. Some processors have reduced unit sizes, weights, and/or package volumes to make their products more affordable by holding prices to previous level</p>
<p style="text-align: justify;">
	The activities of the BH Islamic sect and the GON gasoline subsidy removal have combined to markedly increase the costs of food and food marketing in Nigeria. Nigeria&rsquo;s food security at the moment is under stress and could worsen in the upcoming months if BH activities continue unfettered, and especially if the GON attempts to further decrease the subsidy on gasoline. While Nigeria has been able to &ldquo;muddle&rdquo; through these two developments so far, sustained problems in food production and distribution pose significant threats to the nation&rsquo;s food security.</p>
