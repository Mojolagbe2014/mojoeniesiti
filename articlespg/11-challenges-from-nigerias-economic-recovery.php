      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align:justify">This piece documents the facts of the supply-side recovery in Nigeria in the last few years and identifies the triggers. It then discusses the challenges the new turn of events pose to policy makers, who are only just responding to the previous downturn. Data that have become available since reforms were announced mid 2004 are presented here to show that the economy had indeed entered a recovery phase since 1999, long before the economic team was assembled. Stagnation has finally given way to strong broad-based economic growth. The large fiscal deficits, the financing of which inflicted inflation, devaluation, and high interest rates, have also now given way to fiscal surpluses. Thus, the economy has changed dramatically, but the policy documents are not yet acknowledging the changes, much less reflect them. The growing disconnect between economic facts and government reforms portend grave risks for the sustainability of the recovery. Unanticipated positive external shocks have stimulated supply-side recovery and provided unexpected funds abroad. Nigeria now has abundant resources that can be used to translate the current windfall to improved living standards and additional capacity for economic growth. It is hoped that this piece will stimulate the necessary debate to ensure that the boom is not left unmanaged.</p>

<p style="text-align:justify">1. TRANSITING FROM BUST TO BOOM</p>

<p style="text-align:justify">The global economy is on an impressive expansionary cruise, dragging the Nigerian economy along. It is necessary to pinpoint aspects of the Nigerian economy that positive shocks from abroad are affecting the most, and suggest areas in which domestic policies will complement the external growth impulses, rather than counteract them.</p>

<p style="text-align:justify">THE BUST</p>

<p style="text-align:justify">The global economy had been largely sluggish since the early 1980s, with commodity price crunch inflicting deep real sector stagnation on the Nigerian economy. Nigeria fared woefully in those years, as domestic policy choices added high inflation to the externally induced real economic contraction. Nigeria witnessed nearly two full decades of output and demand contractions in the face of increased volatility in inflation, devaluation and interest rates. Various efforts were made to stabilize and adjust the economic between 1982 and 1988, before concerns about the economic transition were dwarfed by new concerns for political transition from around 1989, when income from transient improvements in commodity prices became available to fuel political experimentation. The experimentation dragged on until 1999, culminating in the present democratic dispensation. By the time the present regime assumed office mid-1999, the global economy was at its worst situation in two decades: economic collapse were widespread among developing economies in East Asia, Latin America, Eastern Europe, and Sub-Saharan Africa, and the predominant expectation was that the US economy, Euro Area and Japan would join the train, and the global economy would move into a freezing deflationary phase. The exact opposite has happened. Developed countries did not catch any of the recession flu. For various reasons, they managed to continue to grow and even faster, providing the developing countries the needed impulse for recovery. Better still, two large developing economies that have long been stagnant, China and India, have unexpectedly joined the growth train, providing the global economy a new vent for expansion.</p>

<p style="text-align:justify">THE POLICY RESPONSE</p>

<p style="text-align:justify">The response of the present government to the economic crunch it met in 1999 took about five years to fashion out: there was a four year recognition lag, as the regime only accepted the need for an economic team by mid-2003; and then another full year action lag, as the economic team only began to announce reform measures by mid-2004. Implementation lag is still underway, and so is the impact lag. Meanwhile, global and national economic conditions have changed. Data that have become available since the reforms were announced suggest the economy had indeed entered a recovery phase since 1999, long before the economic team was assembled. Stagnation has finally given way to strong broad based economic 3 September 2005 growth. The large fiscal deficits, the financing of which inflicted inflation, devaluation, and high interest rates, have also now given way to fiscal surpluses. Thus, the economy has changed dramatically, but the policy documents are not yet acknowledging the changes, much less reflect them. The growing disconnect between economic facts and government reforms portend grave risks for the sustainability of the recovery.</p>

<p style="text-align:justify">2. THE RECOVERY2</p>

<p style="text-align:justify">GDP GROWTH: GDP growth rate in 2004 was 6.1 percent. This is the highest since 1991, and it defines a fourteen-year peak. The annual average growth rate increased from 2.65 percent in the 1994-1998 period to 3.75 in the 1999-2003 period. Thus, the annual average GDP growth rate has exceeded the population growth from 1999 to 2003, but became more than twice as large as the population growth rate in 2004&hellip;.</p>

