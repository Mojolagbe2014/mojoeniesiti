      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p>Imagine you are that child that is being undermined and denied the opportunity to be happy and achieve.&nbsp; How would you feel?&nbsp; It is time we come to the understanding and acceptance of children with mild and severe learning difficulties we term as &ldquo;special needs&rdquo;.&nbsp; There is a place for them in our society and everyone should be obliged to accept and support these children.&nbsp; Children with special needs should be given equal opportunity in our educational system and in every aspect of life from birth.<br />
My interest in these children has led me to engage in continuous research and studies which has yielded positive result in and outside the classroom.&nbsp; I always enjoy seeing the children I work with achieve and stay happy. &nbsp;Over the years, I have gradually learnt how to live in the world of children with special needs and how to support them in achieving the best they can achieve.&nbsp; This attitude is what I would advise everyone to adopt while dealing with the children that ignorant people hide, deny or shy away from.&nbsp; Learning difficulties for children with special needs could range from mild to severe, physical to emotional.&nbsp; From knowledge acquired through study and personal experience, I have found out that although it seems challenging working with special needs children, working with them could be easier if we try to live in their world.<br />
Today, most children with learning difficulties are diagnosed under the umbrella of autism because we say it is broad spectrum.&nbsp; Some children have Sensory Motor problems, Attention Deficit Hyperactivity Disorder, Asperger Syndrome, Down&rsquo;s Syndrome, Speech and Language difficulties, to mention but a few.&nbsp; Whatever the learning difficulty and need, we must first learn to accept these children for who they are because, they did not ask to be born and they are not the cause of their learning impairment.&nbsp; We need to show them love and acceptance.&nbsp; We should research skills on how to support them in achieving their maximum happiness and learning potential. &nbsp;Personally, I have seen some of these children achieve good learning skills with good support from home, school and the government (in the western world).<br />
&nbsp;<br />
Here are some possible ways to help us achieve the best for these children:</p>

<ul>
	<li>Accept the fact that this is real and it is your responsibility to support the child in the best way you and society can.&nbsp; Stop that self-denial!</li>
	<li>Stop blaming yourself or your partner or society for giving you this/these child(ren).&nbsp; Work as a team.</li>
	<li>Give that/these child(ren) the same emotional care and love as you would give any normal child.</li>
	<li>Help these children to help themselves &ndash; Independence and confidence.</li>
	<li>Allow them interact with other children and the world around them so that they are prepared for life and not glued to you.&nbsp; &nbsp;Stop hiding these children and doing things for them.</li>
	<li>Allow these children to learn from their mistakes.&nbsp; Do not use their disability as an excuse for their behaviour. &nbsp;Be firm with them because they are intelligent and can understand.</li>
	<li>Stop judging their abilities.&nbsp; Give them a chance to show you what they can do and encourage them further.</li>
	<li>Allow these special children make their own choices.&nbsp; They should be respected.&nbsp; Their opinion matters because they are human and have rights.</li>
	<li>Celebrate their birthdays like other children do.&nbsp; Make them feel special and priceless.</li>
	<li>Avoid showing them sympathy and ensure that others don&rsquo;t.</li>
	<li>Engage them in early learning from as young as six months.</li>
	<li>Get counselling for yourself and partner.&nbsp; I believe it helps.</li>
	<li>Empower yourself by attending seminars, studying and engaging in pro-active research about skills to support these children.</li>
	<li>Be yourself.&nbsp; Live a normal life. Enjoy life.&nbsp; Do not allow negative feelings and thoughts to rule your life and that of your child.</li>
	<li>Be that inspiration to others.&nbsp; I have met a mum who has two children with severe autism and she has gone ahead to acquire a masters degree through all of it.&nbsp; She always looks elegant and has a positive outlook to life.&nbsp; Her children she says give her joy and are priceless. She takes good care of them and they always look beautiful.&nbsp; I also know a dad who travels the world with his disabled son.&nbsp; This has drawn so much love for the child from friends and family.</li>
</ul>

<p>Finally, I would strongly advise that all educational systems make it mandatory to provide equal learning opportunity for children with special needs.&nbsp; Governments, societies, families and everyone should be enlightened about the importance of accepting these children.&nbsp; The educational system should enable these children achieve the best they can.&nbsp; Structures should be put in place to support families. Awareness should be taken to grass root levels not just cities. Judicial systems should enforce the need to care for and educate these children.&nbsp; We need to stop undermining these special children. Just as every one of us is different, so are they also different in a special way.<br />
&nbsp;</p>
