      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	Think the current business climate is temporary, and that things will eventually get back to normal? Well, you ain&#39;t seen nothing yet! - The death of whole industries, global pandemics, the actions by one individual that affect the whole (think one terrorist with a small nuke in New York), the effects of global warming&mdash;these are just some of the issues experts say we&rsquo;ll be facing, and these are only guesses. Speaking at the 2006 TED conference, creativity expert Sir Kenneth Robinson stated that &ldquo;We have no idea of what&rsquo;s going to happen in the future. No one has a clue about what the world will be like in even five years.&rdquo; And that&rsquo;s as specific and accurate a statement as we can make. Nassim Nicholas Taleb, author of The Black Swan: The Impact of the Highly Improbable, says that we have previously lived in a country he calls Mediocristan, where cause and effect were closely connected because life was simpler and the range of possible events was small. Now, the global community lives in a place he has named Extremistan, in which we are both more interdependent, and at the mercy of &ldquo;the singular, the accidental, the unseen and the unpredicted.&rdquo; The only thing any of us can know for certain is that life will continue to change at a rapid pace. Organizational consultant Peter Vail calls this &ldquo;permanent whitewater,&rdquo; referring to a time of ongoing uncertainty and turbulence.</p>
<p style="text-align: justify;">
	No one is exempt. In my work as a thinking partner, I spend a lot of time speaking to people in all walks of life, from the CEO of a joint venture in Saudi Arabia to a stay-at-home mom who now needs to enter the workforce. Whether they&rsquo;re searching for a job, looking for funding for a startup, trying to stay relevant at age 60 in a large corporation, dealing with lost savings, coping with a big new job that has 100 direct reports, struggling to get donations for a nonprofit, or unemployed and afraid of losing their house, people of all ages and walks of life are scrambling to deal with the vast changes happening today&mdash;in every part of the world.</p>
<p style="text-align: justify;">
	The only thing any of us can know for certain is that life will continue to change at a rapid pace.</p>
<p style="text-align: justify;">
	For most of us, this uncertainty isn&rsquo;t fun. Do you know of the &ldquo;life stress&rdquo; list that rates changes such as moving, death of a spouse, getting married, etc.? The folks who created that list in the sixties estimate that life is 44% more stressful now than it was 50 years ago, and they came up with that estimate&mdash;I admit I have no idea how&mdash;before the 2008 global meltdown. I&rsquo;m not sure we even want to know the new number! So what are we to do? Keep on doing what&rsquo;s worked in the past and hope for the best? Scare ourselves into immobility with doom-and-gloom scenarios? Become Pollyannas with our heads in the sand hoping for the best? As a person who spends her days helping people cope with life, I&rsquo;ve become convinced that the best thing&mdash;perhaps the only thing&mdash;we can do to prepare ourselves for life in the future is to learn how to change. To learn to adapt to whatever circumstances come our way, because we can&rsquo;t predict what they will be.</p>
<p style="text-align: justify;">
	In other words, we need to become expert kayakers. Since we&rsquo;re in permanent whitewater, we can&rsquo;t see exactly where the river will take us or where the submerged rocks are, yet when we&rsquo;re tossed out of the boat, we want to make sure to swim, not sink. Experienced rafters are prepared to get bounced out and recover swiftly. They expect the whitewater. They&rsquo;ve learned to relate to it&mdash; and so should we.</p>
<p style="text-align: justify;">
	Why do I place such emphasis here? Because the ability to adapt is, as far as I can tell, the key indicator of success in these turbulent times. It&rsquo;s the capacity to be flexible and resourceful in the face of ever-changing conditions. To respond in a resilient and productive manner when change is required. Another name for it is agility. In a recent McKinsey survey, 89% of the more than 1500 executives surveyed worldwide ranked agility as very or extremely important to their business success. And 91% said it has become more important over the past five years.</p>
<p style="text-align: justify;">
	According to Webster&rsquo;s, agile means &ldquo;the ability to move with an easy grace; having a quick, resourceful and adaptable character.&rdquo; Webster&rsquo;s has it a bit wrong, I&rsquo;d say. I don&rsquo;t think it has anything to do with character. It&rsquo;s just that some of us already know how to adapt easily. The rest of us need to learn quickly. Otherwise we&rsquo;ll end up spinning our wheels, complaining, or contracting in fear when faced with change.</p>
<p style="text-align: justify;">
	Aikido masters say that to be successful in life, three kinds of mastery are required: mastery with self, which means understanding our feelings and thoughts and how to regulate and direct them; mastery with others, which means being able to create shared understanding and shared action, and; mastery with change, which means having the capacity to adapt easily without losing our center&mdash;our values, talents, and sense of purpose. I&rsquo;m talking here about the third.</p>
<p style="text-align: justify;">
	Do you know how to change easily? I think it&rsquo;s a very rare capacity, because most of us don&rsquo;t understand the science of it or how to work with the way our brains are structured, to make it as painless as possible. Who among us took a class on &ldquo;How to Cope with Change,&rdquo; or had parents who said, &ldquo;Now I&rsquo;m going to teach you how to not just survive in life, but thrive no matter what happens?&rdquo; In the past, changes happened more slowly, and our need to adapt was much, much less. Here&rsquo;s just one example of the acceleration of change: Starting at 1 AD, it took 1500 years for the amount of information in the world to double. It&rsquo;s now doubling at the rate of once every two years. No wonder we&rsquo;re scrambling to keep up!</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	M J Ryan</p>
