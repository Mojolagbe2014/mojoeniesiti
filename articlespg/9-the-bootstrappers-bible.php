	There has never been a better time to start a business with no money. And this manifesto - available for free for a limited time&nbsp; will show you how. This manifesto is based on The Bootstrapper&rsquo;s Bible, a book I wrote a few years ago. What I&rsquo;ve done is divided it into short sections, so you can find the little kernel of insight you need, when you need it. (I hope!)</p>
<p style="text-align: justify;">
	The Joy of Small</p>
<p style="text-align: justify;">
	What&rsquo;s a Bootstrapper?</p>
<p style="text-align: justify;">
	Well, since you bought this manifesto, chances are that you qualify! For me, a bootstrapper is not a particular demographic or even a certain financial situation. Instead, it is a state of mind. Bootstrappers run billion-dollar companies, nonprofit organizations, and start-ups in their basements. A bootstrapper is determined to build a business that pays for itself every day. In many ways, it is easiest to define a bootstrapper by what she is not: a money-raising bureaucrat who specializes in using other peoples money to take big risks in growing a business. Not that there is anything wrong with that&hellip;</p>
<p style="text-align: justify;">
	You can use the information in this manifesto to make any company more focused, more efficient, and more grassroots. Throughout this manifesto, though, I&rsquo;ll be primarily addressing the classic bootstrapper: entrepreneurs who are working their butts off to start a great business from scratch with no (or almost no) money.</p>
<p style="text-align: justify;">
	At last count, there were several million bootstrappers in this country, with another few million wannabes, just waiting for the opportunity. My goal is to give you enough insight and confidence that you&rsquo;ll get off the bench and make it happen.</p>
<p style="text-align: justify;">
	The Bootstrapper&rsquo;s Manifesto</p>
<p style="text-align: justify;">
	TAPE THIS TO YOUR BATHROOM MIRROR AND</p>
<p style="text-align: justify;">
	READ IT OUT LOUD EVERY NIGHT BEFORE YOU GO TO BED:</p>
<p style="text-align: justify;">
	I am a bootstrapper. I have initiative and insight and guts, but not much money. I will succeed because my efforts and my focus will defeat bigger and better-funded competitors. I am fearless. I keep my focus on growing the business&mdash;not on politics, career advancement, or other wasteful distractions. I will leverage my skills to become the key to every department of my company, yet realize that hiring experts can be the secret to my success. I will be a fervent and intelligent user of technology, to conserve my two most precious assets: time and money. My secret weapon is knowing how to cut through bureaucracy. My size makes me faster and more nimble than any company could ever be. I am a laser beam. Opportunities will try to cloud my focus, but I will not waver from my stated goal and plan&mdash;until I change it. And I know that plans were made to be changed. I&rsquo;m in it for the long haul. Building a business that will last separates me from the opportunist, and is an investment in my brand and my future. Surviving is succeeding, and each day that goes by makes it easier still for me to reach my goals.</p>
<p style="text-align: justify;">
	I pledge to know more about my field than anyone else. I will read and learn and teach. My greatest asset is the value I can add to my clients through my efforts. I realize that treating people well on the way up will make it nicer for me on the way back down. I will be scrupulously honest and overt in my dealings, and won&rsquo;t use my position as a fearless bootstrapper to gain unfair advantage. My reputation will follow me wherever I go, and I will invest in it daily and protect it fiercely. I am the underdog. I realize that others are rooting for me to succeed, and I will gratefully accept their help when offered. I also understand the power of favors, and will offer them and grant them whenever I can. I have less to lose than most -- a fact I can turn into a significant competitive advantage. I am a salesperson. Sooner or later, my income will depend on sales, and those sales can be made only by me, not by an emissary, not by a rep. I will sell by helping others get what they want, by identifying needs and filling them. I am a guerrilla. I will be persistent, consistent, and willing to invest in the marketing of myself and my business. I will measure what I do, and won&rsquo;t lie about it to myself or my spouse. I will set strict financial goals and honestly evaluate my performance. I&rsquo;ll set limits on time and money and won&rsquo;t exceed either. Most of all, I&rsquo;ll remember that the journey is the reward. I will learn and grow and enjoy every single day.</p>
