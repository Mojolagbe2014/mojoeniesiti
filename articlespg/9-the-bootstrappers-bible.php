<p style="text-align: justify;">
	I AM A LASER BEAM</p>
<p style="text-align: justify;">
	The big call came just a few months after Michael Joaquin Grey and Matthew Brown had started up their toy company. Would the two San Francisco bootstrappers like their product included in the movie marketing blitz for The Lost World? Nope, said Grey and Brown, who preferred to stick with their vision of gradually building a market for Zoob, their plastic DNAlike building toy.</p>
<p style="text-align: justify;">
	What the bootstrappers feared was a loss of identity. If they hooked up with the celluloid dinosaurs, they&rsquo;d be seen as just another Jurassic spinoff. On their own, they could create a separate brand and not only avoid extinction but create their own world. Eventually, the two even hope to have their own Zoob movies.</p>
