      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	Report Highlights: Nigeria&#39;s agricultural development is constrained by the lack of access to credit for the predominantly smallholder farmers. Efforts by successive governments to address the problem have been largely unsuccessful. Commercial banks in the country perceive agricultural finance to be high-risk. The Central Bank of Nigeria is making efforts to de-risk the sector and encourage banks to lend to farmers.</p>
<p style="text-align: justify;">
	Executive Summary:</p>
<p style="text-align: justify;">
	&nbsp;Agriculture occupies a priority status in Nigeria as the sector serves as the key driver of growth, wealth creation and poverty reduction for a large portion of the population. It is the leading economic activity in the country contributing about 40 per cent of GDP and providing 60 percent of employment in 2010. More than 80 percent of Nigeria&rsquo;s agricultural production is dominated by poor and small-scale producers in the rural areas. This group lacks access to capital to acquire the needed inputs to increase output and incomes. To overcome this challenge, farmers have continued to exploit various avenues by relying on both non-institutional and institutional sources of credit. Efforts and initiatives by successive Nigerian governments to address the problem have produced lower than expected results as rural farmers still encounter difficulties in accessing credit from the formal financial institutions. Many have resorted to self-help and mostly informal financing options.</p>
<p style="text-align: justify;">
	Background:</p>
<p style="text-align: justify;">
	Like many other African countries, Nigeria is primarily agrarian with its abundant land and water resources. Despite the rapid growth of the oil industry over the years, agriculture still accounts for 40% of GDP and provides employment (both formal and informal) for about 60% of Nigerian‟s 150 million people. Nigeria‟s agriculture remains largely subsistence-based with about 80% of agricultural output coming from rural farmers living on less than a dollar per day, earned from farming less than one hectare (2.47 acres). Nigeria has diverse agro-ecological conditions that can support a variety of farming models to create its own green revolution. However, successive administrations neglected agriculture over the years and failed to diversify the economy away from overdependence on the capital-intensive oil sector. Nigeria was once a large net exporter of agricultural products and the sector was the major foreign exchange earner before the advent of oil in 1970s. Nigeria is currently a huge net importer of agricultural products, with such imports exceeding $3 billion in 2010. The country has the potential to return to its previous position if adequate attention is given to agricultural growth policies, finance and provision of rural infrastructure.</p>
<p style="text-align: justify;">
	The fact of the matter is most of the smallholder farmers lack access to capital to acquire the needed inputs to increase their productivity and incomes and reduce their poverty. Farmers require credit to purchase seeds, fertilizers, herbicides, and buy or rent mechanized equipment and related services.</p>
<p style="text-align: justify;">
	Nigerian agricultural policy recognizes the vital role of agriculture finance in attaining the much desired green revolution. A major focus of the policy is to establish a system of sustainable agricultural financing schemes, programs and institutions that could provide micro and macro credit facilities for the small, medium and large-scale producers, processors and marketers. However, public expenditure on agriculture which serves as the bedrock of financing for the sector has consistently fallen short of recommendations. It is therefore not surprising that these policies have failed to achieve the set goals of food self-sufficiency, self reliance, poverty reduction and rural development. Importantly, Nigeria‟s agriculture is abysmally under-financed. At a public forum in early 2011, the Governor of the Central Bank of Nigeria (CBN) was quoted to have said &ldquo;currently agriculture accounts for 40 percent of the GDP, yet it receives only one percent of total commercial bank loans.&rdquo; This is significantly below the level of other developing countries, e.g. Kenya and Brazil which reportedly registers 6 percent and 18 percent, respectively.</p>
<p style="text-align: justify;">
	The Nigeria Agriculture Public Expenditure Review, a collaborative study carried out by the International Food Policy Research Institute (IFPRI) and the World Bank in 2008, revealed that public spending on agriculture was less than 2 percent of federal expenditure during 2001 to 2005. This is far below the 10% goal set by African leaders under the Comprehensive Africa Agricultural Development Program (CAADP). CAADP was established by the AU assembly in 2003 with a focus on improving food security, nutrition, and increasing incomes in Africa&#39;s largely farming based economies. It aims to do this by raising agricultural productivity by at least 6% per year and increasing public investment in agriculture to 10% of national budgets per year. Very low levels of funds are available for activities considered vital for promoting agricultural growth, such basic and applied agricultural research, agricultural extension and capacity building, and agricultural credit and irrigation development. In spite of this poor investment, agriculture contributed on average 32% to the country‟s total GDP during 1996-2000 and 42% between 2001 and 2009. For a successful transformation of the agricultural sector, experts suggest that Nigerian agriculture requires additional annual investments of as much as $8 billion&hellip;.</p>
