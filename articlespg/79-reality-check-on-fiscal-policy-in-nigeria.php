      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><strong>Abstract:</strong> The fact that the 2014-2016 MTEF projects that an increase of N17 trillion in nominal GDP between 2012 and 2016 will be accompanied by a decline in nominal Federal Government revenue and spending suggests that Nigeria&rsquo;s fiscal policy has disconnected from economic reality. Urgent steps are required to reconnect the fiscal processes to the realities of current economic growth.<br />
<br />
A careful reading of the &lsquo;2014-2016 Medium Term Expenditure Framework and Fiscal Strategy Paper: Supporting Growth and Employment Generation&rsquo;, raises some grave concerns about Nigeria&rsquo;s fiscal policy. All that one has to do is to relate the revenue and spending figures in the MTEF to the nominal GDP figures, which are also projected in the MTEF. The MTEF expresses only the deficits as a fraction of GDP, but all other components become even more meaningful as a faction of GDP. Worrying pictures of the fiscal trends emerge.<br />
<br />
<strong>Fiscal Disconnect! </strong>There is a puzzling fiscal disconnect in Nigeria as the nominal GDP is projected to grow in the MTEF from N40.5 trillion in 2012 to N57 trillion in 2016, but the nominal total revenue accruing to the Federal Government is projected to decline from N4.1 trillion in 2013 to 3.98 trillion in 2016, and the nominal total Federal Government expenditure is projected to decline from 4.987 trillion in 2013 to N4.839 trillion in 2016. An increase of N17 trillion in nominal GDP in the next three to four years to be accompanied by a decline in nominal Federal Government revenue and spending suggests that Nigeria&rsquo;s fiscal policy has disconnected from economic reality. Urgent steps are required to reconnect the fiscal processes to the realities of current economic growth.<br />
<br />
Total Federal Government Retained Revenue was N3.63 trillion or 8.95% of GDP in 2012, down from 13.14% of GDP in 2008. In the 2014-2016 MTEF, it is expected to be an unlikely N4.1 trillion or 8.57% of GDP in 2013, N3.58 trillion or 7.45% of GDP in 2014, N3.85 trillion or 7.36% of GDP in 2015, and N3.98 trillion or 6.97% of GDP by 2016. Although the MTEF did promise to plug leakages in oil revenue and revamp the non-oil generation processes, lower nominal revenue projection through 2016 betrays a lack of commitment to such reforms.<br />
<br />
Total Federal Government Expenditure was N4.6 trillion in 2012 or 11.36% of GDP in 2012, down from 13.34% of GDP in 2008. In the 2014-2016 MTEF it is expected to be N4.987 trillion or 10.42% of GDP in 2013, before dropping to N4.495 trillion or 9.35% of GDP in 2014, N4.744 trillion or 9.06% of GDP in 2015, and N4.839 trillion or 8.48% of GDP by 2016. Except the Government finds additional revenue to fund increased spending, promises in the MTEF to transform the economy, focus on job creation, reduce unemployment, especially of women and youth, as well as provide enabling environment for economic diversification and growth, will amount to mere attempts to play on the aspirations of Nigerians without ever delivering on them.<br />
<br />
Capital spending by the Federal Government had fallen from 3.95 % GDP in 2008 to 2.16% of GDP in 2012. Although the MTEF projects capital spending of 3.74% of GDP in 2013, indications are that only about half that level will be utilized. Of the N1.787 trillion voted for capital spending in 2013, the MTEF reveals that only about 430 billion had been utilized by the end of July, out of the 600 billion released and cash backed in the first two quarters, suggesting that total capital vote utilization in 2013 is not likely to be more than the N875 billion utilized in 2012, in spite of a capital vote that is twice as large. Fiscal strategy in the last two years has ostensibly included efforts to correct the imbalance between recurrent and capital spending, but there is no reason to expect any change in this trend through 2016. Resuscitating the capacity to implement capital projects even in the presence of adequate funding has become an urgent national economic policy challenge.<br />
<br />
Nigeria is inadvertently enduring a super tight fiscal policy regime, which the Central bank of Nigeria had incidentally quite erroneously repeatedly portrayed as &lsquo;expansionary&rsquo; (based on reading nominal budget figures in isolation of nominal GDP) and used to justify the imposition of a tight monetary policy regime in the last three years. The combination of inadvertently tight fiscal policy and tight monetary policy will most likely undermine growth, diversification and employment.<br />
<br />
The Nigerian fiscal process has clearly derailed and there is an urgent need to reexamine the agencies charged with fiscal policy management and reorganize them for better performance. The Federal Ministry of Finance is failing in its core duty of ensuring that nominal revenue and spending growth should at least keep pace with nominal economic growth, and now needs to be allowed to concentrate on those core functions. The revenue base will continue to dwindle if the Federal Ministry of Finance continues to be distracted by economic policy management, and the capital vote will continue to be under-implemented if the Federal Ministry of Finance continues be distracted by policy design and coordination pressures. The Federal Ministry of Finance needs to be allowed to focus on the daily pressures of revenue collection, disbursement of payments, and debt management.<br />
<br />
Economic Policy design is not necessarily the function of the treasury. This ought to be located somewhere much higher than the treasury, preferably in the Executive Office of the President. Even budget design is not necessarily the function of the treasury. This too should sensibly be located higher up in the government, preferably in the Executive Office of the President. Once the president determines his budget, the treasury and other operational agencies will implement. The US Office of Budget and Management (OMB) was transferred from the Treasury Department, where it started life as the Bureau of Budget (BOB) in 1921, to the Executive Office of the President in 1939, where the Council of Economic Advisers (CEA) has also helped the President to design his economic policies since 1946. In Nigeria, the Budget Office of the Federation needs to be moved from the Ministry of Finance to the Presidency, and the parallel activities of economic policy design and coordination must also be undertaken from the presidency.</div><br /><br />
