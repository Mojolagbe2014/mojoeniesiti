      <td colspan="3"><div class="description" style="font-size:13px; text-align:justify;"><p style="text-align: justify;">
	Report Highlights: After several years of discussion and debate, the Nigeria Senate passed the Biosafety Bill into law on June 1, 2011. The passage of the law demonstrates that the country is prepared to receive, regulate and most importantly, commercialize biotechnology products. The law leans heavily on the precautionary approach and requires certification and mandatory labeling for imports of all products of biotechnology. In the meantime, Nigeria is currently conducting field trials for transgenic cow pea, sorghum and cassava varieties.</p>
<p style="text-align: justify;">
	Section I. Executive Summary:</p>
<p style="text-align: justify;">
	Nigeria, Africa&rsquo;s most populous nation (150 million), is a food deficit country. Formally a net food exporter, Nigeria&rsquo;s subsistence agriculture can no longer supply the needs of its growing population. According to trade sources, Nigeria imported about $3.7 billion worth of agricultural commodities in 2010. Nigeria is largely a bulk commodity market and imports wheat, soybean products, tallow, rice and high value products. In CY 2010, U.S. agricultural exports to Nigeria reached $834 million, primarily wheat. Nigeria was the second largest buyer of U.S. wheat in the world in 2010/11.</p>
<p style="text-align: justify;">
	Nigeria&rsquo;s biosafety bill, in development for nearly 15 years, was finally enacted into law by the Senate on June 1, 2011. The bill went through the Senate procedure by resolution and concurrence after the required three readings. The Senate concurred without amendments to the draft bill passed by the House of Representatives on July 20, 2010. Officials of the National Biotechnology Development Agency (NABDA) and the Federal Ministry of Environment, who have been driving the process, confirmed that the law has been promptly transmitted to the President and will soon be signed into law. Upon signing into law, the Federal Ministry of Environment, which houses the secretariat of the National Biosafety Committee, is making plans to publicize the law widely.</p>
<p style="text-align: justify;">
	Although the law leans heavily on the precautionary principle, it is seen as a major milestone in ensuring the safe application of biotechnology. Specifically, Nigerians expect the application of the technology to agriculture to serve as a tool to achieve food security. The enactment of the law sends a very clear message to the rest of Africa and indeed the world that the most populated country on the continent is prepared to receive, regulate and, most importantly, commercialize biotechnology products.</p>
<p style="text-align: justify;">
	The enactment of the biosafety law has provided the legal framework for Nigerian scientists who have done much research to move forward from field trials into commercial testing phases for eventual deployment to farmers. Currently, three biotech crops are undergoing field trials in Nigeria: the Bt Cowpea, bio-fortified sorghum and the bio-cassava Plus. Bt Cowpea and the Africa Bio-fortified Sorghum (ABS) are undergoing trials at the Institute for Agricultural Research, ABU, Zaria, while Bio-Cassava Plus is undergoing trial at the National Root Crop Research Institute, Umudike. The transgenic cassava, named &ldquo;Super Cassava,&rdquo; which is fortified with vitamin A was developed at the Danforth Center. Also, there is also growing interest in the testing and prompt release of insect-resistant, herbicide-tolerant cotton from the private sector.</p>
<p style="text-align: justify;">
	The Law calls for the establishment the Biosafety Department under the National Biodiversity Management Agency. The Biosafety Department is expected to be the focal point and authority on Biosafety in the country.</p>
<p style="text-align: justify;">
	According to the Federal Government of Nigeria, the law aims to:</p>
<ul>
	<li style="text-align: justify;">
		Define modules of practice of modern biotechnology and the handling of its products (GMOs) to ensure safety to the environment and to human health.</li>
	<li style="text-align: justify;">
		Guide different segments of society in contributing to safe application of modern biotechnology.</li>
	<li style="text-align: justify;">
		Recognize the complex issues to be addressed by central authorities in the judicious application of modern biotechnology.</li>
	<li style="text-align: justify;">
		Ensure that modern biotechnology activities and their products (GMOs) are safe for the environment and to human health.</li>
	<li style="text-align: justify;">
		Base the deliberate release of GMO on advance informed agreement</li>
	<li style="text-align: justify;">
		Define responsibilities among designated bodies/institutions.</li>
	<li style="text-align: justify;">
		Confer powers to authorize release of GMOs and practice of modern biotechnology activities.</li>
	<li style="text-align: justify;">
		Confers power to carry out risk assessment/management</li>
	<li style="text-align: justify;">
		Define offences and penalty for violation of the act</li>
	<li style="text-align: justify;">
		Cover all genetically modified organisms/living modified organisms, products food/feed and processing.</li>
	<li style="text-align: justify;">
		Cover socio-economic consideration in risk assessment and labeling of all GM products</li>
</ul>
<p style="text-align: justify;">
	The Biosafety Law also defines penalties for not complying with its regulations, and failure to obtain approval or proper permits before importing or releasing GMOs into the environment carry the following stated penalties:</p>
<ul>
	<li style="text-align: justify;">
		Individuals can be fined 2.5 million Naira or imprisonment for a period not less than 5 years or both;</li>
	<li style="text-align: justify;">
		Corporations would pay a fine of not less than 5 million Naira and the directors or officers of the body shall each be liable to a fine not less than 2.5 million Naira or imprisonment for a term not less than 5 years or to both such find and imprisonment.</li>
	<li style="text-align: justify;">
		False information results in the same penalty as failure to obtain approval.</li>
	<li style="text-align: justify;">
		Obstruction results in a 2.5 million Naira fine or imprisonment for not less than 3 years or both.</li>
</ul>
<p style="text-align: justify;">
	The law contains some clauses that could negatively impact the importation of products derived through agricultural biotechnology. Section 9 (functions of the national biosafety committee) mandates the committee to assess and recommend approval of applications submitted for the import/export, transfer, and transit of GMO products. In addition, Part V (Notification and Authorization) clearly states that importation/exportation and movement of GMO products requires prior approval from the biosafety agency (when established) or the Ministry of Environment. Also, the new law requires mandatory labeling of products derived through agricultural biotechnology.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	Section II. Plant Biotechnology Trade and Production:</p>
<p style="text-align: justify;">
	A. Commercial Production of Biotechnology Crops</p>
<p style="text-align: justify;">
	Nigeria does not produce any biotechnology crops commercially. At a recent meeting organized by NABDA, key speakers recommended that Nigeria should commence the commercialization of GM crops starting with crops with high industrial uses.</p>
<p style="text-align: justify;">
	B. Biotechnology Research Efforts</p>
<p style="text-align: justify;">
	Capacity exists at the International Institute for Tropical Agriculture (IITA) and to some extent at the GON&rsquo;s Sheda Science and Technology Complex (SHESTCO), to conduct and apply biotechnology research. Nigerian scientists using the facilities at the IITA have made significant progress in the transformation of a local tomato variety. The institute is doing preliminary work on bio-engineered cowpea. The Bio-cassava Plus undergoing trials was developed in United States by the Plant Danforth Center, Missouri, while the Cowpea was developed in Australia but in both cases with significant participation of Nigerian scientists.</p>
<p style="text-align: justify;">
	C. Biotechnology Crops under Development</p>
<p style="text-align: justify;">
	There is no biotechnology crop under development in Nigeria that will be on the market in the coming year. With transgenic insect-resistant cotton now in commercial production in Burkina Faso, Nigeria farmers have indicated strong interest in commercial production of GMOs crops, such as bioengineered cotton and the genetically modified water efficient corn.</p>
<p style="text-align: justify;">
	D. Imports of Biotechnology Crops/Products</p>
<p style="text-align: justify;">
	At present, agricultural products such as soybeans, soybean meal, soybean oil, and corn are freely imported from the U.S., EU, Brazil and Argentina.</p>
<p style="text-align: justify;">
	E. Food Aid</p>
<p style="text-align: justify;">
	Nigeria is not a recipient of food aid.</p>
<p style="text-align: justify;">
	F. Production of Biotechnology Crops Developed Outside the United States</p>
<p style="text-align: justify;">
	At present, Nigeria does not produce biotechnology crops&hellip;. &nbsp;&nbsp;</p>
